// Глава 13: Модуль stream - все о process в Node.js

// В Node.js модуль stream предоставляет интерфейсы для работы с потоками данных.
// Потоки являются основным способом работы с потоковыми данными, такими как файлы, сети и другие входные/выходные источники.

// process - это глобальный объект, который предоставляет информацию и контроль над текущим процессом Node.js.
// Он может быть использован для взаимодействия с процессом и управления его жизненным циклом.

// Основные свойства и методы объекта process:

// process.argv - массив аргументов командной строки:
process.argv.forEach((val, index) => {
  console.log(`${index}: ${val}`);
});

// process.env - объект, содержащий переменные окружения:
console.log('Переменные окружения:', process.env);

// process.exit() - метод для завершения текущего процесса:
process.exit(0);

// process.stdin - поток для ввода данных (Readable):
process.stdin.on('data', (data) => {
  console.log(`Получено через stdin: ${data.toString()}`);
});

// process.stdout - поток для вывода данных (Writable):
process.stdout.write('Hello, stdout!\n');

// process.stderr - поток для вывода ошибок (Writable):
process.stderr.write('Hello, stderr!\n');

// Итог:
// Модуль stream в Node.js предоставляет мощные и гибкие инструменты для работы с потоками данных.
// Глобальный объект process предоставляет множество методов и свойств для взаимодействия и управления текущим процессом Node.js.
