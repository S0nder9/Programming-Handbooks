// Глава 5: Стек вызовов
// Установка и настройка Prettier

// Prettier - это популярный инструмент для форматирования кода, который помогает поддерживать единый стиль кода в проекте.
// Prettier автоматически форматирует код на основе заданных правил, что позволяет сократить время на ручное форматирование и избежать ошибок.

// Установка Prettier
// Для установки Prettier в проект используйте npm (Node Package Manager). 
// Откройте терминал и выполните следующую команду:

// npm install --save-dev prettier

// Это установит Prettier как зависимость разработки (devDependency) в вашем проекте.

// Настройка Prettier
// Создайте файл конфигурации Prettier в корне вашего проекта. Обычно это файл с именем .prettierrc.

// Пример .prettierrc:
{
	"semi": true, // добавлять точку с запятой в конце строк
		"singleQuote": true, // использовать одинарные кавычки вместо двойных
			"tabWidth": 2, // ширина табуляции
				"trailingComma": "es5", // добавлять запятые в конце списков (ES5 стандарт)
					"printWidth": 80, // максимальная длина строки
						"arrowParens": "always" // всегда добавлять скобки вокруг параметров стрелочных функций
}

// Можно также создать файл .prettierignore, чтобы указать файлы и папки, которые Prettier должен игнорировать:

// Пример .prettierignore:
node_modules
build
dist

// Интеграция с редакторами кода
// Prettier можно интегрировать с большинством популярных редакторов кода, таких как VSCode, Sublime Text, Atom и другие.
// Для интеграции с VSCode установите расширение Prettier - Code formatter из магазина расширений VSCode.

// Пример настройки Prettier в VSCode:
// Откройте настройки VSCode (Ctrl + ,) и добавьте следующие параметры:
{
	"editor.formatOnSave": true, // автоматически форматировать код при сохранении файла
		"prettier.requireConfig": true // использовать только файлы, которые имеют конфигурацию Prettier
}

// Использование Prettier
// После установки и настройки Prettier вы можете форматировать код вручную или автоматически при сохранении файлов.
// Для ручного форматирования откройте командную палитру (Ctrl + Shift + P) и выберите "Format Document" или "Format Selection".

// Пример использования Prettier для форматирования кода:
function exampleFunction(param1, param2) {
	if (param1 > param2) {
		return param1 - param2;
	} else {
		return param2 - param1;
	}
}

// После форматирования Prettier приведет код к единому стилю:
function exampleFunction(param1, param2) {
	if (param1 > param2) {
		return param1 - param2;
	} else {
		return param2 - param1;
	}
}

// Итог:
// Установка и настройка Prettier позволяет автоматизировать процесс форматирования кода, что помогает поддерживать единый стиль в проекте и улучшает читаемость кода.
