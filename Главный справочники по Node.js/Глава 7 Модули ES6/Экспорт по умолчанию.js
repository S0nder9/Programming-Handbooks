// Глава 7: Модули ES6 - Экспорт по умолчанию в Node.js

// В ES6 (ECMAScript 2015) был введен новый синтаксис для работы с модулями, который упрощает экспорт и импорт модулей.
// Один из важных аспектов новых модулей ES6 - это экспорт по умолчанию, который позволяет экспортировать одну функцию, объект или значение как основной экспорт модуля.

// Как работают модули с экспортом по умолчанию:
// В ES6 каждый файл считается модулем.
// Чтобы экспортировать единственный объект, функцию или значение, используется ключевое слово `export default`.
// `export default` может использоваться только один раз в модуле.

// Пример экспорта по умолчанию функции:
const myFunction = () => {
    console.log('Hello from myFunction');
  };
  
  export default myFunction;
  
  // В другом файле можно импортировать этот модуль с помощью `import`:
  import importedFunction from './myModule.js';
  importedFunction(); // Выведет: Hello from myFunction
  
  // Пример экспорта по умолчанию объекта:
  const myObject = {
    name: 'John',
    age: 30,
  };
  
  export default myObject;
  
  // В другом файле можно импортировать этот объект с помощью `import`:
  import importedObject from './myModule.js';
  console.log(importedObject.name); // Выведет: John
  
  // Пример экспорта по умолчанию значения:
  const myValue = 42;
  
  export default myValue;
  
  // В другом файле можно импортировать это значение с помощью `import`:
  import importedValue from './myModule.js';
  console.log(importedValue); // Выведет: 42
  
  // Итог:
  // Использование экспорта по умолчанию позволяет легко экспортировать и импортировать одну функцию, объект или значение как основной экспорт модуля.
