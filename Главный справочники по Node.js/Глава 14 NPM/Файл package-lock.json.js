// Глава 14: NPM - Допустимые версии, Файл package-lock.json в Node.js

// NPM (Node Package Manager) является стандартным менеджером пакетов для Node.js. 
// Он используется для установки, обновления и управления зависимостями вашего проекта.

// Допустимые версии:
// В NPM используются семантические версии (semver), которые состоят из трех чисел: MAJOR.MINOR.PATCH. 
// Эти числа указывают на большие изменения, новые функции и исправления ошибок соответственно.

// Примеры допустимых версий:
// 1. Точная версия: "1.2.3" - это указывает на конкретную версию пакета.
// 2. Диапазон версий:
//    - ">=1.2.3" - версия 1.2.3 и выше
//    - "<1.2.3" - все версии ниже 1.2.3
//    - "1.2.x" - любые версии, где MAJOR=1 и MINOR=2, а PATCH может быть любым
//    - "~1.2.3" - любая версия от 1.2.3 до (но не включая) 1.3.0
//    - "^1.2.3" - любая версия от 1.2.3 до (но не включая) 2.0.0

// Файл package-lock.json:
// Файл package-lock.json автоматически создается при установке зависимостей с помощью NPM.
// Он фиксирует точные версии установленных пакетов, что помогает обеспечить одинаковую установку зависимостей на всех машинах.
// Этот файл важен для консистентности и предсказуемости установки пакетов.

