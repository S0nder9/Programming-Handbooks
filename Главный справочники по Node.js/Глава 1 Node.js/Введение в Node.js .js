// Глава 1: Введение в Node.js

// Node.js - это среда выполнения JavaScript, построенная на движке V8, используемом в Google Chrome.
// Node.js позволяет запускать JavaScript вне браузера, что делает его идеальным для создания серверных приложений и утилит.

// Основные характеристики Node.js:
// - Асинхронная и событийно-ориентированная архитектура
// - Высокая производительность благодаря движку V8
// - Богатая экосистема модулей и библиотек через npm (Node Package Manager)

// Пример: Создание простого HTTP сервера на Node.js

// Для создания HTTP сервера в Node.js используется встроенный модуль 'http'.
// В этом примере мы создадим сервер, который будет обрабатывать HTTP запросы и отправлять ответы.

const http = require('http');

// Создаем сервер
const server = http.createServer((req, res) => {
  // Устанавливаем заголовки ответа
  res.writeHead(200, { 'Content-Type': 'text/plain' });
  
  // Отправляем ответ
  res.end('Hello, World!\n');
});

// Устанавливаем сервер на прослушивание порта 3000
server.listen(3000, '127.0.0.1', () => {
  console.log('Server is running at http://127.0.0.1:3000/');
});

// Запуск сервера:
// Чтобы запустить сервер, сохраните этот код в файл, например, server.js, и выполните команду в терминале:
// node server.js
// После этого сервер будет доступен по адресу http://127.0.0.1:3000/

// Итог:
// Node.js предоставляет возможность запускать JavaScript на серверной стороне, что делает его мощным инструментом для создания серверных приложений.
// Пример создания HTTP сервера показывает, как легко можно начать работать с Node.js и создавать серверы для обработки запросов.
