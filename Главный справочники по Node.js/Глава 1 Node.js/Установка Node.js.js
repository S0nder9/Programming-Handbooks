// Глава 1: Установка Node.js

// Node.js — это среда выполнения JavaScript вне браузера, которая позволяет создавать серверные приложения. 
// Установка Node.js включает в себя установку самого Node.js и пакетного менеджера npm (Node Package Manager).

// Установка Node.js:
// 1. Зайдите на официальный сайт Node.js (https://nodejs.org/).
// 2. Выберите подходящую версию для вашей операционной системы (LTS версия рекомендуется для большинства пользователей).
// 3. Скачайте установочный файл и следуйте инструкциям по установке.

// После установки, проверьте, что Node.js и npm установлены корректно, выполнив следующие команды в командной строке или терминале:
console.log("Проверка версии Node.js:");
console.log("node -v"); // Должен вывести версию Node.js, например, v14.17.0

console.log("Проверка версии npm:");
console.log("npm -v"); // Должен вывести версию npm, например, 6.14.13