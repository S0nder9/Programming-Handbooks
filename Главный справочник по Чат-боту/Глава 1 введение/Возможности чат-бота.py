# Пример создания простого чат-бота
# В этом примере мы создадим простого чат-бота, который будет отвечать на команды /start и /help.

# Установка библиотеки python-telegram-bot:
# pip install python-telegram-bot

from telegram import Update
from telegram.ext import Updater, CommandHandler, CallbackContext

# Функция, обрабатывающая команду /start
def start(update: Update, context: CallbackContext) -> None:
    update.message.reply_text('Привет! Я ваш чат-бот в Telegram. Чем могу помочь?')

# Функция, обрабатывающая команду /help
def help_command(update: Update, context: CallbackContext) -> None:
    update.message.reply_text('Вы можете использовать следующие команды:\n/start - Начать взаимодействие\n/help - Получить справку')

# Основная функция для запуска бота
def main() -> None:
    # Токен, полученный от BotFather
    TOKEN = 'YOUR_TELEGRAM_BOT_TOKEN'
    
    # Создаем объект Updater и передаем ему токен бота
    updater = Updater(TOKEN)

    # Получаем диспетчер для регистрации обработчиков
    dispatcher = updater.dispatcher

    # Регистрируем обработчики команд /start и /help
    dispatcher.add_handler(CommandHandler("start", start))
    dispatcher.add_handler(CommandHandler("help", help_command))

    # Запускаем бота
    updater.start_polling()

    # Ожидаем сигналов для остановки бота
    updater.idle()

# Запуск основной функции
if __name__ == '__main__':
    main()

