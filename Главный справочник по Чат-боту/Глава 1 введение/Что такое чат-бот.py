# Глава 1: Введение - ЧТО ТАКОЕ ЧАТ-БОТ в Telegram

# Чат-бот - это программное приложение, которое взаимодействует с пользователями через текстовые или голосовые интерфейсы.
# В Telegram чат-боты предоставляют автоматические ответы на запросы пользователей, выполняют задачи и предоставляют информацию в режиме реального времени.

# Зачем нужны чат-боты?
# Чат-боты используются для автоматизации задач, таких как предоставление поддержки пользователям, выполнение заказов, обработка запросов и многое другое.
# Они могут работать круглосуточно и быстро отвечать на запросы, что делает их эффективным инструментом для бизнеса и других областей.

# Как создать чат-бота в Telegram?
# Чтобы создать чат-бота в Telegram, необходимо:
# 1. Создать нового бота с помощью BotFather.
# 2. Получить токен для доступа к API вашего бота.
# 3. Написать код для взаимодействия с Telegram API с использованием токена.

# Пример создания простого чат-бота с использованием библиотеки python-telegram-bot
# Для этого потребуется установить библиотеку: pip install python-telegram-bot

from telegram import Update
from telegram.ext import Updater, CommandHandler, CallbackContext

# Функция для обработки команды /start
def start(update: Update, context: CallbackContext) -> None:
    update.message.reply_text('Привет! Я ваш чат-бот в Telegram.')

# Основная функция для запуска бота
def main() -> None:
    # Токен вашего бота, который вы получили от BotFather
    token = 'YOUR_BOT_TOKEN_HERE'
    
    # Создаем экземпляр Updater и передаем ему токен
    updater = Updater(token)
    
    # Получаем диспетчер для регистрации обработчиков
    dispatcher = updater.dispatcher
    
    # Регистрируем обработчик для команды /start
    dispatcher.add_handler(CommandHandler('start', start))
    
    # Запускаем бота
    updater.start_polling()
    
    # Работаем до тех пор, пока не будет получен сигнал остановки
    updater.idle()

# Проверяем, вызывается ли скрипт напрямую (а не импортируется в качестве модуля)
if __name__ == '__main__':
    main()

