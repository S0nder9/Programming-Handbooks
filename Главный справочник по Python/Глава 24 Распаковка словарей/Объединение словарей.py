# Глава 24: Распаковка словарей - Объединение словарей

# В Python словари (dict) являются одним из основных типов данных для хранения пар "ключ-значение".
# Иногда возникает необходимость объединить несколько словарей в один.
# В Python 3.5 и более поздних версиях доступны удобные способы для объединения словарей.

# Зачем используется объединение словарей?
# Объединение словарей используется, когда нужно комбинировать данные из нескольких источников в один словарь.
# Это может быть полезно, например, при объединении конфигурационных настроек из разных файлов или объединении результатов из различных API запросов.

# Как выполняется объединение словарей?
# Существует несколько способов объединения словарей в Python, включая использование метода update(), оператор ** для распаковки словарей, 
# а также оператора | (pipe) для объединения словарей, начиная с Python 3.9.

# Несколько примеров

# Пример 1: Объединение словарей с помощью метода update()
dict1 = {'a': 1, 'b': 2}
dict2 = {'b': 3, 'c': 4}

# Метод update() обновляет dict1, добавляя в него ключи и значения из dict2
dict1.update(dict2)
print("Объединение с помощью update():", dict1)

# Пример 2: Объединение словарей с помощью распаковки оператором **
dict1 = {'a': 1, 'b': 2}
dict2 = {'b': 3, 'c': 4}

# Использование распаковки оператором ** для создания нового объединенного словаря
combined_dict = {**dict1, **dict2}
print("Объединение с помощью распаковки:", combined_dict)

# Пример 3: Объединение словарей с помощью оператора |
dict1 = {'a': 1, 'b': 2}
dict2 = {'b': 3, 'c': 4}

# Использование оператора | для объединения словарей (Python 3.9+)
combined_dict = dict1 | dict2
print("Объединение с помощью оператора |:", combined_dict)

# Примечание: при объединении словарей, если имеются дублирующиеся ключи, значение из последнего словаря будет перезаписывать значение из предыдущего.

# Итог
# Объединение словарей - это важная операция при работе с данными в Python.
# Существует несколько способов для объединения словарей, включая метод update(), оператор ** и оператор |.
# Эти инструменты позволяют легко и эффективно комбинировать данные из нескольких источников.
