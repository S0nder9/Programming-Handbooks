# Глава 20: Колбэк функции

# Колбэк функции (callback functions) - это функции, которые передаются в другие функции в качестве аргументов и вызываются позже.
# Они широко используются в асинхронном программировании и позволяют выполнять определенные действия после завершения другой функции.

# Зачем используются колбэк функции?
# Колбэк функции используются для выполнения кода после завершения асинхронной операции, например, после завершения запроса к серверу или завершения длительного вычисления.
# Это позволяет писать более гибкий и модульный код, где функции могут быть переданы в качестве аргументов и вызываться по мере необходимости.

# Где используются колбэк функции?
# Колбэк функции используются в различных сценариях, таких как обработка событий, асинхронное программирование, выполнение задач после завершения другой задачи.
# Например, в веб-разработке колбэк функции часто используются для обработки ответов от сервера или обработки событий пользовательского интерфейса.

# Как создаются и используются колбэк функции?
# Колбэк функции создаются как обычные функции и передаются в другие функции в качестве аргументов.
# Они вызываются в нужный момент времени внутри основной функции.

# Несколько примеров

# Пример 1: Простая колбэк функция
def greet(name):
    print(f"Привет, {name}!")

def process_user_input(callback):
    user_input = input("Введите ваше имя: ")
    callback(user_input)

# Передаем функцию greet в качестве колбэка
process_user_input(greet)

# Пример 2: Колбэк функция в асинхронной операции
import time

def async_operation(callback):
    print("Выполняется асинхронная операция...")
    time.sleep(2)  # Симуляция долгой операции
    callback("Результат операции")

def handle_result(result):
    print(f"Получен результат: {result}")

# Передаем функцию handle_result в качестве колбэка
async_operation(handle_result)

# Итог
# Колбэк функции являются важным инструментом для управления асинхронными операциями и обработки событий.
# Они позволяют писать более гибкий и модульный код, улучшая структуру и читабельность программ.
# Понимание и использование колбэк функций помогает создавать более эффективные и отзывчивые приложения.
