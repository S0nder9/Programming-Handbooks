# Глава 34: Объекты и классы

# Создание экземпляров класса

# В Python объекты и классы являются основными элементами объектно-ориентированного программирования (ООП).
# Класс - это шаблон или "чертеж" для создания объектов. Он определяет атрибуты и методы, которые будут у объектов этого класса.
# Экземпляр класса - это отдельный объект, созданный на основе класса.

# Зачем используются классы и объекты?
# Классы и объекты используются для моделирования реальных сущностей и их поведения в программном коде.
# Они помогают организовывать код, делая его более понятным и управляемым.
# Например, вы можете создать класс "Автомобиль" и создавать экземпляры этого класса для различных автомобилей с разными характеристиками.

# Как создается экземпляр класса?
# Экземпляр класса создается путем вызова класса как функции. Например: my_car = Car("Toyota", "Camry", 2020).
# Этот вызов создает новый объект (экземпляр) класса Car с указанными аргументами.

# Актуально ли использование классов и объектов?
# Использование классов и объектов актуально в современных программных разработках, так как позволяет создавать гибкие и масштабируемые приложения.
# ООП помогает решать задачи, связанные с кодом, который легко поддерживать и расширять.

# Пример создания класса и экземпляров

class Car:
    def __init__(self, make, model, year):
        # Метод __init__ инициализирует атрибуты объекта
        self.make = make
        self.model = model
        self.year = year

    def get_description(self):
        # Метод для получения описания автомобиля
        return f"{self.year} {self.make} {self.model}"

    def start_engine(self):
        # Метод для запуска двигателя
        return "Двигатель заведен"

# Создание экземпляров класса Car
car1 = Car("Toyota", "Camry", 2020)
car2 = Car("Honda", "Civic", 2019)

# Использование методов экземпляров
print(car1.get_description())  # Выводит: 2020 Toyota Camry
print(car2.get_description())  # Выводит: 2019 Honda Civic
print(car1.start_engine())     # Выводит: Двигатель заведен

# Итог
# Создание экземпляров класса является ключевым аспектом объектно-ориентированного программирования.
# Оно позволяет моделировать реальные объекты и их поведение в программном коде, что делает код более структурированным и удобным для поддержки.
# Понимание и умение создавать классы и экземпляры классов является важным навыком для разработки сложных программных систем.
