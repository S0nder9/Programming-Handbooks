# Операторы in и not in

# Операторы in и not in в Python используются для проверки наличия элемента в последовательности, такой как строка, список, кортеж или множество.
# Эти операторы возвращают булевое значение: True, если элемент найден в последовательности, и False, если нет.

# Оператор in
# Оператор in проверяет, содержится ли указанный элемент в последовательности.

# Пример:
# Проверка, содержится ли символ 'a' в строке
string = "apple"
result = 'a' in string
print("Содержится ли 'a' в строке 'apple'?", result)  # Вывод: True

# Проверка, содержится ли число 3 в списке
numbers = [1, 2, 3, 4, 5]
result = 3 in numbers
print("Содержится ли число 3 в списке [1, 2, 3, 4, 5]?", result)  # Вывод: True

# Оператор not in
# Оператор not in проверяет, не содержится ли указанный элемент в последовательности.

# Пример:
# Проверка, не содержится ли символ 'b' в строке
string = "apple"
result = 'b' not in string
print("Не содержится ли 'b' в строке 'apple'?", result)  # Вывод: True

# Проверка, не содержится ли число 6 в списке
numbers = [1, 2, 3, 4, 5]
result = 6 not in numbers
print("Не содержится ли число 6 в списке [1, 2, 3, 4, 5]?", result)  # Вывод: True

# Зачем используются операторы in и not in?
# Эти операторы широко используются для различных задач, таких как проверка наличия элемента в коллекции данных, фильтрация данных, 
# поиск подстрок в строках и многое другое. Они помогают писать более читаемый и эффективный код.

# Как использовать операторы in и not in?
# Операторы in и not in могут быть использованы с любыми последовательностями: строками, списками, кортежами, множествами и словарями.

# Примеры использования операторов in и not in

# Пример с кортежем
tuple_data = (10, 20, 30, 40)
result = 20 in tuple_data
print("Содержится ли 20 в кортеже (10, 20, 30, 40)?", result)  # Вывод: True

# Пример с множеством
set_data = {1, 2, 3, 4, 5}
result = 10 not in set_data
print("Не содержится ли 10 в множестве {1, 2, 3, 4, 5}?", result)  # Вывод: True

# Пример со словарем (поиск по ключам)
dict_data = {'a': 1, 'b': 2, 'c': 3}
result = 'a' in dict_data
print("Содержится ли ключ 'a' в словаре {'a': 1, 'b': 2, 'c': 3}?", result)  # Вывод: True

# Итог
# Операторы in и not in являются мощными инструментами для проверки наличия элементов в различных последовательностях.
# Они обеспечивают простоту и читаемость кода, а также помогают эффективно выполнять задачи по обработке данных.

