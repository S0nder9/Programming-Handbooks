# Глава 41: Работа с датами и временем - Форматирование дат

# В Python для работы с датами и временем существует модуль datetime, который предоставляет классы для управления датами и временем.
# Форматирование дат – это процесс преобразования объектов даты и времени в строки и обратно, чтобы они соответствовали определенному шаблону.

# Зачем используется форматирование дат?
# Форматирование дат необходимо для отображения даты и времени в удобочитаемом виде, а также для преобразования дат из строкового формата обратно в объекты даты и времени для дальнейшей обработки.

# Где используется форматирование дат?
# Форматирование дат используется во многих приложениях, где необходимо отображать или обрабатывать дату и время.
# Например, при создании отчетов, логировании событий, работе с временными метками и многом другом.

# Как выполнять форматирование дат?
# Для форматирования дат в Python используется метод strftime() класса datetime, который позволяет преобразовать объект даты в строку по заданному формату.
# Для преобразования строки в объект даты используется метод strptime().

import datetime

# Получение текущей даты и времени
now = datetime.datetime.now()

# Пример форматирования даты
formatted_date = now.strftime("%Y-%m-%d %H:%M:%S")
print("Текущая дата и время в формате 'ГГГГ-ММ-ДД ЧЧ:ММ:СС':", formatted_date)

# Пример преобразования строки в объект даты
date_string = "2023-08-03 12:45:00"
date_object = datetime.datetime.strptime(date_string, "%Y-%m-%d %H:%M:%S")
print("Преобразованная строка в объект даты:", date_object)

# Форматирование даты с различными форматами
formatted_date_1 = now.strftime("%d/%m/%Y")
formatted_date_2 = now.strftime("%B %d, %Y")
formatted_date_3 = now.strftime("%I:%M %p")

print("Формат 'ДД/ММ/ГГГГ':", formatted_date_1)
print("Формат 'Месяц ДД, ГГГГ':", formatted_date_2)
print("Формат 'ЧЧ:ММ ПП':", formatted_date_3)

# Итог
# Форматирование дат является важной частью работы с датами и временем в Python.
# Модуль datetime предоставляет удобные методы для преобразования объектов даты и времени в строки и обратно, позволяя форматировать даты в соответствии с любыми требованиями.
