# Глава 37: JSON - JSON

# JSON (JavaScript Object Notation) - это текстовый формат обмена данными, который является простым и легким для чтения и записи людьми.
# Он часто используется для передачи данных между сервером и веб-приложением в формате, который может быть легко обработан языками программирования, такими как JavaScript, Python и другими.

# В Python для работы с JSON существует встроенный модуль json, который позволяет кодировать и декодировать данные в формате JSON.

# Зачем используется JSON?
# JSON используется для обмена данными между сервером и клиентом, хранения конфигурационных данных и передачи структурированных данных через API.
# Он является популярным выбором из-за своей простоты и широкого распространения.

# Как использовать JSON в Python?
# В Python модуль json предоставляет два основных метода: json.dumps() для сериализации (преобразования объекта Python в строку JSON) и json.loads() для десериализации (преобразования строки JSON обратно в объект Python).

import json

# Примеры

# Пример 1: Сериализация объекта Python в JSON
data = {
    "name": "John",
    "age": 30,
    "city": "New York"
}

# Преобразование объекта Python в строку JSON
json_string = json.dumps(data)
print("Сериализованный JSON:", json_string)

# Пример 2: Десериализация строки JSON в объект Python
json_data = '{"name": "John", "age": 30, "city": "New York"}'

# Преобразование строки JSON в объект Python
data = json.loads(json_data)
print("Десериализованные данные:", data)

# Пример 3: Работа с файлами JSON

# Сохранение объекта Python в файл в формате JSON
with open('data.json', 'w') as json_file:
    json.dump(data, json_file)

# Чтение данных из файла в формате JSON
with open('data.json', 'r') as json_file:
    data_from_file = json.load(json_file)
print("Данные, прочитанные из файла:", data_from_file)

# Итог
# JSON является важным инструментом для обмена данными между различными системами и приложениями.
# В Python модуль json позволяет легко кодировать и декодировать данные в формате JSON, а также работать с JSON файлами.
# Понимание работы с JSON является важной частью разработки современных приложений и работы с API.

