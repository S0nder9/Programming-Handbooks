# Глава 11: Словари - Длина словаря

# Словари (dictionaries) - это один из наиболее важных и часто используемых типов данных в Python.
# Они позволяют хранить пары ключ-значение и обеспечивают быстрый доступ к значениям по их ключам.

# Длина словаря - это количество пар ключ-значение, содержащихся в словаре.
# В Python для получения длины словаря используется встроенная функция len().

# Зачем используется длина словаря?
# Определение длины словаря может быть полезным в различных сценариях, например:
# - Проверка, пуст ли словарь.
# - Определение количества элементов, чтобы использовать эту информацию в логике программы.
# - Итерация по элементам словаря с учетом их количества.

# Как получить длину словаря?
# Для получения длины словаря достаточно передать словарь в функцию len(). 
# Например: len(my_dict)

# Несколько примеров

# Пример 1: Получение длины словаря
my_dict = {
    "apple": 3,
    "banana": 5,
    "orange": 2
}
print("Длина словаря my_dict:", len(my_dict))

# Пример 2: Проверка, пуст ли словарь
empty_dict = {}
if len(empty_dict) == 0:
    print("Словарь empty_dict пуст.")
else:
    print("Словарь empty_dict содержит элементы.")

# Пример 3: Использование длины словаря в логике программы
inventory = {
    "яблоки": 10,
    "бананы": 6,
    "апельсины": 8
}
if len(inventory) > 5:
    print("Много разных фруктов в наличии!")
else:
    print("Количество различных фруктов ограничено.")

# Итог
# Длина словаря в Python определяется с помощью функции len(), которая возвращает количество пар ключ-значение в словаре.
# Это полезная операция, которая может быть использована для проверки содержимого словаря, реализации логики программы и других задач.