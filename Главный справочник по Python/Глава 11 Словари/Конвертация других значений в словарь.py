# Глава 11: Словари

# Конвертация других значений в словарь

# В Python словари являются одним из наиболее часто используемых типов данных. Они представляют собой коллекции пар "ключ-значение".
# В этой главе мы рассмотрим, как конвертировать другие типы данных в словари.

# Зачем конвертировать другие значения в словарь?
# Конвертация других типов данных в словари может быть полезна в различных сценариях, таких как преобразование данных для удобного доступа по ключу,
# создание конфигураций, передача данных между функциями и многое другое.

# Как конвертировать другие значения в словарь?
# В Python существуют различные способы конвертации других типов данных в словарь. Рассмотрим основные из них:

# 1. Конвертация списка кортежей в словарь
# Можно преобразовать список кортежей, где каждый кортеж состоит из двух элементов (ключа и значения), в словарь с помощью функции dict().

list_of_tuples = [("key1", "value1"), ("key2", "value2"), ("key3", "value3")]
dict_from_list_of_tuples = dict(list_of_tuples)
print("Словарь из списка кортежей:", dict_from_list_of_tuples)

# 2. Конвертация списка списков в словарь
# Аналогично можно преобразовать список списков, где каждый вложенный список состоит из двух элементов (ключа и значения), в словарь.

list_of_lists = [["key1", "value1"], ["key2", "value2"], ["key3", "value3"]]
dict_from_list_of_lists = dict(list_of_lists)
print("Словарь из списка списков:", dict_from_list_of_lists)

# 3. Конвертация строки в словарь
# Можно использовать метод json.loads() из модуля json для преобразования строки в формате JSON в словарь.

import json

json_string = '{"key1": "value1", "key2": "value2", "key3": "value3"}'
dict_from_json_string = json.loads(json_string)
print("Словарь из JSON строки:", dict_from_json_string)

# 4. Конвертация двух списков в словарь
# Можно создать словарь из двух списков, один из которых содержит ключи, а другой - значения, с помощью функции zip() и dict().

keys = ["key1", "key2", "key3"]
values = ["value1", "value2", "value3"]
dict_from_two_lists = dict(zip(keys, values))
print("Словарь из двух списков:", dict_from_two_lists)

# Итог
# Конвертация других значений в словарь является важной техникой в Python, которая позволяет эффективно управлять данными.
# Знание различных способов конвертации помогает разработчикам работать с данными более гибко и удобно.
