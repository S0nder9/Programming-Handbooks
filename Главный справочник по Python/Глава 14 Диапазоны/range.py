# Глава 14: Диапазоны - range

# В Python диапазоны чисел можно создавать с помощью встроенной функции range().
# Эта функция генерирует последовательность чисел, которые могут быть использованы в различных циклах и других конструкциях.

# Что такое range?
# Функция range() возвращает объект, который генерирует арифметическую прогрессию целых чисел.
# Существует три различных способа использования функции range():
# 1. range(stop)
# 2. range(start, stop)
# 3. range(start, stop, step)

# 1. range(stop)
# Этот вариант создает последовательность чисел от 0 до stop, не включая stop.
# Пример:
for i in range(5):
    print(i)
# Вывод:
# 0
# 1
# 2
# 3
# 4

# 2. range(start, stop)
# Этот вариант создает последовательность чисел от start до stop, не включая stop.
# Пример:
for i in range(2, 7):
    print(i)
# Вывод:
# 2
# 3
# 4
# 5
# 6

# 3. range(start, stop, step)
# Этот вариант создает последовательность чисел от start до stop, не включая stop, с шагом step.
# Пример:
for i in range(1, 10, 2):
    print(i)
# Вывод:
# 1
# 3
# 5
# 7
# 9

# Где используется range?
# Функция range() часто используется в циклах for для повторения определенного блока кода заданное количество раз.
# Она также может быть использована для создания списков чисел или других итераций, где требуется последовательность чисел.

# Актуально ли?
# Функция range() является актуальной и широко используемой в Python.
# Она предоставляет удобный и эффективный способ создания последовательностей чисел и управления итерациями в циклах.

# Несколько примеров

# Пример использования range() в цикле for для итерации по списку
numbers = [10, 20, 30, 40, 50]
for i in range(len(numbers)):
    print(f"Элемент под индексом {i}: {numbers[i]}")

# Пример создания списка чисел с использованием range()
number_list = list(range(5, 15))
print("Список чисел от 5 до 14:", number_list)

# Пример использования range() с отрицательным шагом
for i in range(10, 0, -2):
    print(i)
# Вывод:
# 10
# 8
# 6
# 4
# 2

# Итог
# Функция range() является мощным инструментом для создания последовательностей чисел в Python.
# Она широко используется в циклах for и других конструкциях, где требуется повторение или итерация.
# Понимание различных способов использования функции range() помогает эффективно управлять циклами и последовательностями в ваших программах.