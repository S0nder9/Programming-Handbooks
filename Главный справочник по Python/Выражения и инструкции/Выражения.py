# Выражения

# В Python выражения - это комбинации значений, переменных, операторов и вызовов функций, которые интерпретируются и вычисляются для получения значения.
# Проще говоря, выражение - это любой код, который Python может вычислить и вернуть как результат.

# Зачем используются выражения?
# Выражения используются для выполнения различных вычислений и операций в программе.
# Они могут включать арифметические операции, логические операции, операции со строками и многое другое.
# Выражения являются основой для выполнения задач, начиная от простых вычислений до сложной логики программ.

# Где используются выражения?
# Выражения используются повсюду в Python-программах:
# - В присваиваниях: переменная = выражение
# - В условных операторах: if выражение: 
# - В циклах: while выражение:
# - В функциях: def функция(параметры): return выражение
# - И в любом месте, где требуется вычислить какое-то значение

# Как создавать выражения?
# Выражения создаются путем комбинирования значений (например, чисел, строк), переменных и операторов (+, -, *, / и т.д.).
# Примеры выражений включают:
# - 3 + 5 (арифметическое выражение)
# - "Hello, " + "world!" (строковое выражение)
# - x > 10 (логическое выражение)
# - len("example") (вызов функции)

# Примеры выражений

# Арифметические выражения
a = 10
b = 5
sum_result = a + b  # 15
difference = a - b  # 5
product = a * b  # 50
quotient = a / b  # 2.0

print("Сумма:", sum_result)
print("Разница:", difference)
print("Произведение:", product)
print("Частное:", quotient)

# Логические выражения
is_greater = a > b  # True
is_equal = a == b  # False

print("a больше b:", is_greater)
print("a равно b:", is_equal)

# Строковые выражения
greeting = "Hello"
name = "Alice"
full_greeting = greeting + ", " + name + "!"  # "Hello, Alice!"

print(full_greeting)

# Вызов функции в выражении
length_of_greeting = len(full_greeting)  # 13

print("Длина приветствия:", length_of_greeting)

# Итог
# Выражения являются фундаментальной частью программирования на Python.
# Они позволяют выполнять вычисления и логические операции, комбинируя значения, переменные и операторы.
# Понимание выражений и умение их использовать является ключевым навыком для написания эффективного кода на Python.
