# Глава 3: Выражения и инструкции

# Инструкции в Python

# Инструкции в Python - это команды, которые интерпретатор выполняет для выполнения определенных операций.
# Они могут включать в себя присваивание значений переменным, выполнение арифметических операций, управление потоком программы, вызов функций и многое другое.
# Инструкции являются основными строительными блоками любой программы.

# Зачем используются инструкции?
# Инструкции используются для выполнения различных операций и управления потоком программы.
# Они позволяют вам создавать переменные, выполнять вычисления, принимать решения, повторять определенные действия и т.д.

# Где используются инструкции?
# Инструкции используются повсеместно в программировании на Python. Они являются основой любой программы или скрипта.
# Без инструкций невозможно было бы реализовать даже самые простые задачи.

# Как использовать инструкции?
# В Python инструкции записываются построчно. Каждая строка кода представляет собой отдельную инструкцию.
# Вот основные типы инструкций, которые часто используются:

# 1. Присваивание значений
a = 5
b = 10

# 2. Арифметические операции
sum = a + b
difference = b - a
product = a * b
quotient = b / a

# 3. Управляющие структуры (условия и циклы)
if a < b:
    print("a меньше b")
else:
    print("a не меньше b")

for i in range(5):
    print("Итерация номер", i)

# 4. Вызов функций
def greet(name):
    return f"Привет, {name}"

greeting = greet("Мир")
print(greeting)

# Несколько примеров

# Пример 1: Присваивание значений и арифметические операции
x = 7
y = 3
result = x ** y  # возведение в степень
print("Результат возведения в степень:", result)

# Пример 2: Условные инструкции
number = 15
if number % 2 == 0:
    print("Число четное")
else:
    print("Число нечетное")

# Пример 3: Циклы
for char in "Python":
    print(char)

# Итог
# Инструкции являются фундаментальной частью программирования в Python.
# Они позволяют выполнять различные операции, управлять потоком программы и взаимодействовать с данными.
# Понимание и умение использовать инструкции является ключевым навыком для любого программиста на Python.
