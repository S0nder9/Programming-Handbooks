# Глава 26: Соединение строк - Форматирование строк с f-strings

# Форматирование строк - это процесс создания строк с включением значений переменных и выражений внутри них.
# В Python есть несколько способов форматирования строк, и одним из наиболее удобных и современных является использование f-strings (или форматированных строковых литералов).

# Что такое f-strings?
# f-strings, введенные в Python 3.6, позволяют включать выражения в строки, заключая их в фигурные скобки {} и предшествуя строке буквой 'f'.
# Это делает код более читаемым и удобным для написания и понимания по сравнению с более старыми методами форматирования строк.

# Зачем используются f-strings?
# f-strings используются для вставки значений переменных, выражений и результатов функций прямо в строку.
# Это полезно для создания динамических сообщений, форматированного вывода данных и генерации строк с включением вычисляемых значений.

# Как создаются и используются f-strings?
# Для создания f-строки необходимо добавить перед строкой букву 'f' и заключить выражения, которые вы хотите вставить, в фигурные скобки {}.
# Пример: f"Ваше имя {name}"

# Примеры использования f-strings

# Пример 1: Вставка переменной в строку
name = "Alice"
greeting = f"Привет, {name}!"
print(greeting)  # Вывод: Привет, Alice!

# Пример 2: Вставка выражения в строку
x = 5
y = 10
result = f"Сумма {x} и {y} равна {x + y}"
print(result)  # Вывод: Сумма 5 и 10 равна 15

# Пример 3: Вызов функции внутри f-строки
def greet(name):
    return f"Привет, {name}!"

message = f"{greet('Bob')}, как дела?"
print(message)  # Вывод: Привет, Bob!, как дела?

# Пример 4: Форматирование чисел
value = 123.45678
formatted_value = f"Отформатированное значение: {value:.2f}"
print(formatted_value)  # Вывод: Отформатированное значение: 123.46

# Пример 5: Использование f-strings для вывода данных в таблицу
data = [
    {"name": "Alice", "age": 30},
    {"name": "Bob", "age": 25},
    {"name": "Charlie", "age": 35}
]

for person in data:
    print(f"Имя: {person['name']:<10} Возраст: {person['age']:>3}")

# Итог
# f-strings являются мощным и удобным инструментом для форматирования строк в Python.
# Они позволяют легко и понятно вставлять значения переменных и выражений в строки, улучшая читаемость и удобство написания кода.
# Использование f-strings рекомендуется в тех случаях, когда требуется создать динамическую строку с включением вычисляемых значений или переменных.