# Комментарии в Python

# Комментарии - это текст в коде программы, который интерпретатор Python игнорирует.
# Они используются для того, чтобы пояснить код и сделать его более понятным для людей, которые его читают.
# Комментарии помогают разработчикам помнить, что делает конкретный блок кода, и могут быть полезны при совместной работе над проектом.

# Зачем используются комментарии?
# Комментарии используются для того, чтобы:
# 1. Объяснить, что делает конкретный участок кода.
# 2. Оставить заметки для других разработчиков или для себя в будущем.
# 3. Временно отключить часть кода без его удаления (для отладки).

# Виды комментариев в Python:
# 1. Однострочные комментарии
# 2. Многострочные комментарии

# Как использовать комментарии в Python?
# Однострочные комментарии начинаются с символа # и распространяются до конца строки.
# Многострочные комментарии заключаются в тройные кавычки (''' или """).

# Примеры комментариев

# Однострочный комментарий
# Этот комментарий объясняет следующую строку кода, которая выводит "Hello, World!" на экран.
print("Hello, World!")  # Выводит приветственное сообщение на экран

# Многострочный комментарий
'''
Этот многострочный комментарий объясняет следующий блок кода.
Он используется для демонстрации различных операций со строками в Python.
'''

text = "Python - это мощный язык программирования."
# Преобразование строки в нижний регистр
lower_case = text.lower()
print("Строка в нижнем регистре:", lower_case)

# Замена подстроки
replaced_text = text.replace("мощный", "популярный")
print("Измененная строка:", replaced_text)

# Итог
# Комментарии в Python являются важным инструментом для документирования кода и улучшения его читаемости.
# Правильное использование комментариев помогает лучше понять, как работает код, и облегчает совместную разработку.
