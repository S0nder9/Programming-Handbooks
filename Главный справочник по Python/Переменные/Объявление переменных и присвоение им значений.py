# Глава 4: Переменные

# Объявление переменных и присвоение им значений

# В Python переменные используются для хранения данных, которые могут быть использованы и изменены в течение работы программы.
# Переменная - это именованное место в памяти, где хранятся данные.

# Как объявить переменную?
# В Python переменные не требуют явного объявления перед использованием. 
# Просто присвойте значение переменной, и Python автоматически создаст её.

# Присвоение значений переменным
# Присвоение значения переменной выполняется с использованием оператора равенства (=).
# Левая сторона оператора равенства получает имя переменной, а правая сторона - значение, которое присваивается переменной.

# Примеры объявления переменных и присвоения значений

# Пример 1: Присвоение числового значения переменной
age = 25
print("Возраст:", age)

# Пример 2: Присвоение строкового значения переменной
name = "Alice"
print("Имя:", name)

# Пример 3: Присвоение значения переменной с использованием математического выражения
sum_result = 5 + 3
print("Сумма:", sum_result)

# Пример 4: Присвоение значения переменной на основе ввода пользователя
user_input = input("Введите ваше любимое число: ")
favorite_number = int(user_input)  # преобразование строки в целое число
print("Ваше любимое число:", favorite_number)

# Объявление нескольких переменных в одной строке
x, y, z = 10, 20, 30
print("x:", x, "y:", y, "z:", z)

# Присвоение одного значения нескольким переменным
a = b = c = 100
print("a:", a, "b:", b, "c:", c)

# Итог
# Объявление переменных и присвоение им значений - это базовые концепции программирования, необходимые для работы с данными.
# В Python это делается просто и интуитивно, что позволяет быстро и легко начать работу с переменными и данными.
