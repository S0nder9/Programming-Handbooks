// Глава 3: Состояние и Работа с Событиями

// Добавление двустороннего связывания

// Двустороннее связывание в React позволяет автоматически связывать значения полей ввода с состоянием компонента, 
// так что при изменении состояния автоматически обновляется значение поля ввода и наоборот.

// В React это часто используется для управления формами.

// Пример использования двустороннего связывания:

import React, { useState } from 'react';

function TwoWayBinding() {
  // Создаем состояние для хранения значения поля ввода
  const [inputValue, setInputValue] = useState('');

  // Обработчик изменения значения в поле ввода
  const handleChange = (event) => {
    // Обновляем состояние значения поля ввода
    setInputValue(event.target.value);
  };

  return (
    <div>
      {/* Используем значение из состояния для установки начального значения поля ввода */}
      <input 
        type="text" 
        value={inputValue} 
        onChange={handleChange} 
      />
      {/* Отображаем текущее значение из состояния */}
      <p>Введенное значение: {inputValue}</p>
    </div>
  );
}

export default TwoWayBinding;
