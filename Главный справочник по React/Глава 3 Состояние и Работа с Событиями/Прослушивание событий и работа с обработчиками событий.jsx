// Глава 3: Состояние и Работа с Событиями

// Прослушивание событий и работа с обработчиками событий

// В React прослушивание событий осуществляется через добавление атрибутов-обработчиков в элементы JSX.
// Обработчики событий представляют собой функции, которые будут вызываться при возникновении определенных событий.

import React, { useState } from 'react';

function App() {
  // Объявляем состояние для хранения данных о кликах
  const [clickCount, setClickCount] = useState(0);

  // Обработчик клика
  const handleClick = () => {
    // Увеличиваем количество кликов на 1
    setClickCount(prevCount => prevCount + 1);
  };

  return (
    <div>
      {/* Прослушивание кликов на кнопке */}
      <button onClick={handleClick}>Нажми меня</button>
      {/* Выводим количество кликов */}
      <p>Количество кликов: {clickCount}</p>
    </div>
  );
}

export default App;
