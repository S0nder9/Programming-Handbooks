// Глава 5: Стилизация компонентов

// Знакомство со Styled Components

// Styled Components - это библиотека для стилизации компонентов в React приложениях.
// Она позволяет писать CSS стили непосредственно в JavaScript коде, что упрощает управление стилями и их переиспользование.

// Шаг 1: Установка Styled Components
// Сначала необходимо установить библиотеку Styled Components с помощью npm или yarn:
// npm install styled-components
// или
// yarn add styled-components

// Шаг 2: Импорт Styled Components
// После установки библиотеки ее можно импортировать в нужный файл React компонентов:
import styled from 'styled-components';

// Шаг 3: Создание стилизованного компонента
// Для создания стилизованного компонента используется функция styled, которая принимает имя HTML элемента или другой компонент React.
// Затем внутри этой функции можно определить CSS стили с помощью шаблонной строки, используя специальный синтаксис.

// Пример:
const Button = styled.button`
    background-color: #3498db;
    color: white;
    padding: 10px 20px;
    border: none;
    border-radius: 5px;
    cursor: pointer;

    &:hover {
        background-color: #2980b9;
    }
`;

// В этом примере создается стилизованный компонент Button, который рендерит HTML кнопку с определенными CSS стилями.
// CSS стили определены внутри строки шаблона (между обратными кавычками), где используется синтаксис CSS.

// Шаг 4: Использование стилизованного компонента
// После создания стилизованного компонента он может быть использован в других компонентах так же, как и обычный HTML или React компонент.

// Пример использования Button:
const MyComponent = () => {
    return (
        <div>
            <Button>Click me</Button>
        </div>
    );
};

// В этом примере компонент MyComponent рендерит кнопку, созданную с помощью стилизованного компонента Button.
// При наведении на кнопку меняется ее фоновый цвет, так как это определено в CSS стилях для состояния :hover.
