// Передача данных при помощи props

// В React компоненты могут принимать данные через свойство props (сокращение от properties).
// Props являются объектом, который содержит все переданные компоненту данные.
// Давайте создадим простой компонент, который принимает данные через props и выводит их на экран.

// Пример компонента, принимающего и отображающего данные через props
import React from 'react';

// Компонент, который принимает имя и возраст и выводит их на экран
function Person(props) {
  return (
    <div>
      <p>Привет, меня зовут {props.name}.</p>
      <p>Мне {props.age} лет.</p>
    </div>
  );
}

// Использование компонента Person с передачей данных через props
function App() {
  return (
    <div>
      {/* Передаем данные в компонент Person через атрибуты props */}
      <Person name="Алиса" age={30} />
      <Person name="Боб" age={25} />
    </div>
  );
}

export default App;
