// Глава 2: Основы React

// Как работает React
// React - это JavaScript-библиотека для создания пользовательских интерфейсов. 
// Он использует компонентную архитектуру, которая позволяет разбивать интерфейс на множество небольших и переиспользуемых компонентов.
// React работает с виртуальным DOM, что позволяет ему эффективно обновлять только необходимые части интерфейса.

// Компонент <App/>
// Компонент <App/> является корневым компонентом вашего приложения React. 
// Это обычно самый высокоуровневый компонент, который содержит другие компоненты, 
// такие как заголовок, боковое меню, основное содержимое и т. д.

// Пример кода в файле JSX:

import React from 'react';

// Определение компонента <App/>
function App() {
    return (
        <div>
            <h1>Привет, мир!</h1>
            <p>Это мой первый компонент React.</p>
        </div>
    );
}

export default App;

// Этот компонент <App/> просто отображает заголовок и абзац с текстом "Привет, мир!" и "Это мой первый компонент React."
// Этот код на JSX представляет собой комбинацию JavaScript и HTML, 
// которая позволяет вам описывать структуру пользовательского интерфейса в виде компонентов React.
