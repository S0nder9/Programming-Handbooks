// Альтернативный синтаксис функций
// В React существует несколько способов определения компонентов.
// Один из них - использование альтернативного синтаксиса функций (также называемый "функциональные компоненты").

// Пример функционального компонента с использованием альтернативного синтаксиса функций:
import React from 'react';

// Компонент "Приветствие", который принимает props и возвращает JSX.
// Здесь используется стрелочная функция для определения компонента.
const Greeting = (props) => {
    // Возвращаем JSX, который отображает приветствие с именем из props.
    return <h1>Hello, {props.name}!</h1>;
};

// Экспортируем компонент "Приветствие", чтобы его можно было использовать в других частях приложения.
export default Greeting;
