// Шаг 1: Установка Create React App
// В терминале выполните команду установки Create React App:
// npm install -g create-react-app
// Это установит глобально инструмент Create React App, который позволит быстро создать новый проект React.

// Шаг 2: Создание нового проекта
// В терминале перейдите в папку, где вы хотите создать новый проект, и выполните команду:
// npx create-react-app my-react-app
// Это создаст новый проект React с именем "my-react-app" в текущей папке.
// После создания проекта вы можете перейти в его директорию:
// cd my-react-app

// Шаг 3: Запуск проекта
// После создания проекта, чтобы запустить его, выполните следующие команды в терминале:
// cd my-react-app
// npm start
// Это запустит проект React в режиме разработки. По умолчанию он будет доступен по адресу http://localhost:3000/.

// Шаг 4: Изучение структуры проекта
// После создания проекта вы можете изучить его структуру:
// - В папке src находится основной код вашего приложения.
// - Файл index.js является точкой входа в ваше приложение.
// - Файл App.js содержит основной компонент вашего приложения.
// - Файл App.css содержит стили для компонента App.
// - В папке public находятся статические файлы вашего приложения.

// Шаг 5: Редактирование кода
// Вы можете редактировать код в файлах src/App.js, src/index.js и других файлах, чтобы создать свое приложение.

// Шаг 6: Просмотр результатов
// После внесения изменений в код вы можете увидеть результаты в браузере, который автоматически обновляется при сохранении изменений.

// Шаг 7: Развертывание проекта
// После завершения разработки вы можете развернуть свой проект React на хостинге, таком как Netlify, Vercel или GitHub Pages.

// Функция компонента App в файле App.js:

// Эта функция App является компонентом React. Она представляет собой функциональный компонент, который возвращает JSX (JavaScript XML).
// JSX - это расширение языка JavaScript, которое позволяет писать HTML-подобный код внутри JavaScript.
// Компонент App возвращает корневой элемент JSX <div>, содержащий заголовок <h1> с текстом "Start!".

function App() {
  return (
    <div>
      <h1>Start!</h1>
    </div>
  );
}

export default App;

// Импорт ReactDOM в файле index.js:

// ReactDOM - это библиотека React, предназначенная для взаимодействия с DOM (Document Object Model).
// Функция createRoot() создает корневой элемент React для приложения. Она принимает в качестве аргумента DOM-элемент, в который будет встроено React-приложение.
// В данном случае createRoot() используется для создания корневого элемента с id "root" из HTML-файла, в который будет встроено приложение.

import ReactDOM from 'react-dom/client';

const root = ReactDOM.createRoot(document.getElementById('root'));

// Рендеринг компонента App в корневой элемент:

// Метод render() принимает React-элемент или компонент и встраивает его в указанный DOM-элемент.
// В данном случае метод render() используется для рендеринга компонента App в корневой элемент с id "root".
// Таким образом, компонент App будет отображен внутри элемента, который имеет id "root" в HTML-файле.

root.render(
    <App />
);

// После выполнения этого кода компонент App будет отображен на веб-странице внутри элемента с id "root".
