// Декларативное программирование в React означает описание того, **что** должно быть отображено на экране, а не **как** это должно быть достигнуто.
// Вот пример декларативного подхода к созданию компонента в React:

import React from 'react';

// Объявляем функциональный компонент
function Greeting(props) {
  return <h1>Hello, {props.name}!</h1>;
}

// Используем компонент
function App() {
  return (
    <div>
      <Greeting name="Alice" />
      <Greeting name="Bob" />
    </div>
  );
}

export default App;

// В этом примере компонент Greeting отображает приветствие с именем, которое передается через свойство name.
// Компонент App использует Greeting дважды с разными именами.

// Декларативное программирование делает код более читаемым и понятным, так как он описывает желаемый результат.
// React берет на себя задачу оптимизации DOM и обновления компонентов в соответствии с изменениями данных.
