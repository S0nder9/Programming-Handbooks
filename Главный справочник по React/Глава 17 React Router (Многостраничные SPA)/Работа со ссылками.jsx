// Глава 17: React Router (Многостраничные SPA)

// React Router — это библиотека для создания маршрутизации в React-приложениях. Она позволяет создавать многостраничные приложения с помощью маршрутов.
// Работа со ссылками — это важный аспект при создании маршрутизации. С помощью компонента Link можно навигировать между различными страницами приложения без перезагрузки.

// Основные компоненты React Router:
// 1. BrowserRouter — оборачивает все приложение и позволяет использовать маршрутизацию.
// 2. Route — определяет маршрут и компонент, который должен быть отображен при переходе по этому маршруту.
// 3. Switch — используется для группировки нескольких компонентов Route. Гарантирует, что будет отображен только один маршрут.
// 4. Link — используется для создания ссылок для навигации между страницами.

import React from 'react';
import { BrowserRouter as Router, Route, Switch, Link } from 'react-router-dom';

// Пример компонентов, которые будут отображаться на разных страницах
const Home = () => (
  <div>
    <h2>Главная</h2>
    <p>Добро пожаловать на главную страницу!</p>
  </div>
);

const About = () => (
  <div>
    <h2>О нас</h2>
    <p>Это страница "О нас".</p>
  </div>
);

const Contact = () => (
  <div>
    <h2>Контакты</h2>
    <p>Свяжитесь с нами!</p>
  </div>
);

// Основной компонент приложения с маршрутизацией
const App = () => {
  return (
    <Router>
      <div>
        {/* Навигационные ссылки */}
        <nav>
          <ul>
            <li>
              <Link to="/">Главная</Link>
            </li>
            <li>
              <Link to="/about">О нас</Link>
            </li>
            <li>
              <Link to="/contact">Контакты</Link>
            </li>
          </ul>
        </nav>

        {/* Определение маршрутов */}
        <Switch>
          <Route exact path="/">
            <Home />
          </Route>
          <Route path="/about">
            <About />
          </Route>
          <Route path="/contact">
            <Contact />
          </Route>
        </Switch>
      </div>
    </Router>
  );
}

export default App;

// Итог:
// React Router позволяет легко создавать многостраничные SPA, предоставляя простые и удобные средства для маршрутизации и навигации.
// Компоненты Link и Route обеспечивают навигацию и отображение соответствующих страниц, что делает работу с многокомпонентными приложениями эффективной и интуитивно понятной.
