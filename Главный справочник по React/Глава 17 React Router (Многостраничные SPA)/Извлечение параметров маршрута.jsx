// Глава 17: React Router (Многостраничные SPA)

// Извлечение параметров маршрута

// React Router предоставляет удобные средства для работы с параметрами маршрута. 
// Это позволяет передавать данные через URL и использовать их в компонентах, что особенно полезно для создания многостраничных SPA.

// Для извлечения параметров маршрута используется хук useParams из react-router-dom.

// Пример использования useParams для извлечения параметров маршрута:

import React from 'react';
import { BrowserRouter as Router, Route, Switch, Link, useParams } from 'react-router-dom';

// Компонент для отображения деталей пользователя
function UserDetail() {
  // Извлекаем параметр userId из URL
  const { userId } = useParams();

  return (
    <div>
      <h2>Детали пользователя</h2>
      <p>Идентификатор пользователя: {userId}</p>
    </div>
  );
}

// Основной компонент приложения
function App() {
  return (
    <Router>
      <div>
        <nav>
          <ul>
            <li>
              <Link to="/user/1">Пользователь 1</Link>
            </li>
            <li>
              <Link to="/user/2">Пользователь 2</Link>
            </li>
            <li>
              <Link to="/user/3">Пользователь 3</Link>
            </li>
          </ul>
        </nav>

        <Switch>
          {/* Маршрут для отображения деталей пользователя */}
          <Route path="/user/:userId">
            <UserDetail />
          </Route>
        </Switch>
      </div>
    </Router>
  );
}

export default App;

// Пояснение:
// 1. Компонент UserDetail использует хук useParams для извлечения параметра userId из URL. 
//    Этот параметр указывается в маршруте как :userId и может содержать любое значение, переданное через URL.
// 2. В компоненте App определены маршруты с помощью компонента Route и Switch из react-router-dom. 
//    Каждый маршрут определяет шаблон URL, который должен совпадать с текущим путем.
// 3. Ссылки (Link) используются для навигации по различным маршрутам. 
//    При клике на ссылку пользователь перенаправляется на соответствующий маршрут, и компонент UserDetail отображает идентификатор пользователя, переданный в URL.

// Итог:
// Извлечение параметров маршрута с помощью useParams — это простой и эффективный способ передавать данные через URL в React-приложениях с использованием React Router. 
