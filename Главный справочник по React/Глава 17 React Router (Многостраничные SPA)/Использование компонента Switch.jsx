// Глава 17: React Router (Многостраничные SPA)

// Использование компонента Switch

// React Router является мощной библиотекой для создания многостраничных приложений (SPA) с помощью React.
// Компонент Switch используется для рендеринга первого подходящего маршрута (Route) внутри него.
// Это позволяет создавать несколько маршрутов и гарантировать, что будет отображен только один из них.

// Основные компоненты React Router:
// 1. BrowserRouter - оборачивает все приложение и предоставляет функциональность маршрутизации.
// 2. Route - определяет маршрут и компонент, который будет отображен при совпадении пути.
// 3. Switch - оборачивает маршруты и рендерит первый подходящий маршрут.
// 4. Link - используется для создания ссылок для навигации между страницами.

// Пример использования компонентов React Router, включая Switch:

import React from 'react';
import { BrowserRouter as Router, Route, Switch, Link } from 'react-router-dom';

// Пример компонентов для различных маршрутов
function Home() {
  return <h2>Главная страница</h2>;
}

function About() {
  return <h2>О нас</h2>;
}

function Contact() {
  return <h2>Контакты</h2>;
}

function NotFound() {
  return <h2>Страница не найдена</h2>;
}

function App() {
  return (
    <Router>
      <div>
        {/* Навигационное меню */}
        <nav>
          <ul>
            <li>
              <Link to="/">Главная</Link>
            </li>
            <li>
              <Link to="/about">О нас</Link>
            </li>
            <li>
              <Link to="/contact">Контакты</Link>
            </li>
          </ul>
        </nav>

        {/* Компонент Switch для управления маршрутизацией */}
        <Switch>
          <Route exact path="/">
            <Home />
          </Route>
          <Route path="/about">
            <About />
          </Route>
          <Route path="/contact">
            <Contact />
          </Route>
          <Route>
            <NotFound />
          </Route>
        </Switch>
      </div>
    </Router>
  );
}

export default App;

// Итог:
// Компонент Switch в React Router позволяет создавать многостраничные SPA, 
// рендеря первый подходящий маршрут и обеспечивая управление навигацией в приложении. 
// В данном примере показаны основные принципы использования компонентов BrowserRouter, Route, Switch и Link 
// для создания простого многостраничного приложения с маршрутами для главной страницы, страницы "О нас", 
// страницы "Контакты" и страницы "404" (не найдено).
