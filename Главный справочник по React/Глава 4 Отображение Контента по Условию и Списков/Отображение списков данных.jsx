// Отображение списков данных

// Отображение списков данных в React позволяет динамически выводить элементы на основе данных,
// хранящихся в массиве или объекте. Для этого используется метод map() массивов JavaScript.

// Пример: Отображение списка пользователей

// Предположим, у нас есть массив объектов, представляющих пользователей:
const users = [
    { id: 1, name: 'John', age: 30 },
    { id: 2, name: 'Alice', age: 25 },
    { id: 3, name: 'Bob', age: 35 }
];

// Для отображения списка пользователей в React мы можем использовать метод map():
const userList = users.map(user => (
    <div key={user.id}>
        <p>Name: {user.name}</p>
        <p>Age: {user.age}</p>
    </div>
));

// Здесь мы проходимся по каждому элементу массива пользователей с помощью метода map(),
// и для каждого пользователя создаем блок <div>, содержащий имя и возраст.

// Важно помнить о добавлении атрибута key к каждому элементу списка. 
// React использует атрибут key для эффективного обновления и перерисовки элементов списка.

// Теперь переменная userList содержит список React-элементов,
// которые могут быть вставлены в компонент или шаблон для отображения списка пользователей.
