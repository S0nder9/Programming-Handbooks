// Глава 1: Введение в React

// Создание Single Page Applications (SPA) при помощи React
// React - это JavaScript-библиотека для создания пользовательских интерфейсов.
// Она позволяет разрабатывать эффективные и динамичные веб-приложения, включая Single Page Applications (SPA).
// SPA - это веб-приложения, которые загружаются один раз и динамически обновляют содержимое без перезагрузки страницы.

// Что такое SPA и зачем используется
// SPA являются современным подходом к веб-разработке, позволяющим создавать более быстрые и отзывчивые веб-приложения.
// В SPA весь контент загружается при первой загрузке страницы, а затем динамически обновляется с помощью JavaScript,
// без необходимости переходить на новые страницы. Это позволяет пользователям более плавно взаимодействовать с приложением.

// Пример кода в файле JSX (расширение для файлов React)
// Файлы с расширением .jsx используются для написания компонентов React.
// Ниже приведен пример кода в файле JSX для простого компонента React.

import React from 'react';

// Пример простого компонента React
class MyApp extends React.Component {
    render() {
        return (
            <div>
                <h1>Hello, React!</h1>
                <p>This is a simple React component.</p>
            </div>
        );
    }
}

// Экспорт компонента для использования в других частях приложения
export default MyApp;
