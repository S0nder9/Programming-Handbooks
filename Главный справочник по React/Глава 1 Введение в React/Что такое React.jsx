// Глава 1: Введение в React

// React - это библиотека JavaScript для создания пользовательских интерфейсов.
// Он позволяет разработчикам создавать многокомпонентные веб-приложения, которые могут эффективно обновляться при изменении данных без перезагрузки страницы.
// React использует компонентный подход, где интерфейс разбивается на множество независимых компонентов, каждый из которых управляет своим состоянием и рендерит свою часть пользовательского интерфейса.

// Пример React-компонента
import React from 'react';

