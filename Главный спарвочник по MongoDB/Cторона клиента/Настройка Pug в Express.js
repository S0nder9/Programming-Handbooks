// Глава 2: Создание клиентской части - Настройка Pug в Express

// В этом разделе мы рассмотрим, как настроить Pug в Express.js для создания серверной части, поддерживающей динамические HTML-шаблоны.
// Pug (ранее Jade) — это шаблонизатор, который позволяет создавать HTML-шаблоны, используя минималистичный синтаксис.

// Шаги для настройки Pug в проекте Express:

// 1. Установка Pug
// Перед использованием Pug убедитесь, что он установлен. Вы можете добавить его в проект с помощью следующей команды:
// npm install pug

// 2. Настройка движка представления
// Чтобы использовать Pug в приложении Express, необходимо установить его в качестве движка для представлений (view engine).
// Это можно сделать с помощью метода app.set.

// Подключение модуля path для работы с путями
const path = require("path");
const express = require("express");
const app = express();

// Установка Pug в качестве движка представлений
app.set("view engine", "pug");

// Указание директории для хранения файлов шаблонов Pug
app.set("views", path.join(__dirname, "views"));

// 3. Настройка статической директории
// Для подключения CSS, изображений и других статических файлов мы можем использовать метод express.static.
// В этом примере создается статическая директория "public", где хранятся все такие ресурсы.

app.use(express.static(path.join(__dirname, "public")));

// 4. Создание маршрута для рендеринга шаблона
// Определим базовый маршрут для рендеринга шаблона Pug. Метод res.render() позволяет отрендерить шаблон и отправить его клиенту.
// В примере ниже, Pug-файл "base" рендерится и возвращается в виде HTML.

app.get("/", (req, res, next) => {
  res.status(200).render("base"); // Рендеринг шаблона "base"
});

// 5. Запуск сервера
// Для завершения настройки запустим сервер, чтобы он был доступен на порту 3000.

app.listen(3000, () => {
  console.log("Server is running on http://localhost:3000");
});

// Итог:
// Настройка Pug в Express позволяет использовать мощные шаблоны для динамического HTML.
// Мы установили Pug как движок представлений, указали директории для шаблонов и статических файлов, а также настроили маршрут для рендеринга базового шаблона.
