// Глава 1: Введение в MongoDB
// Подглава 1.1: Установка MongoDB

// MongoDB - это NoSQL база данных, которая хранит данные в формате JSON-подобных документов.
// В этой главе мы рассмотрим, как установить MongoDB на вашу машину, чтобы начать использовать её в своих проектах.

// Шаги по установке MongoDB:

// 1. Скачайте установочный файл MongoDB
// Перейдите на официальный сайт MongoDB: https://www.mongodb.com/try/download/community
// Выберите вашу операционную систему и загрузите соответствующий установочный файл.

// 2. Установите MongoDB
// Для Windows:
// - Запустите загруженный установочный файл и следуйте инструкциям мастера установки.
// - Выберите "Complete" (Полная установка) для установки всех необходимых компонентов.
// - Убедитесь, что установлены компоненты MongoDB Server и MongoDB Compass (GUI для работы с MongoDB).

// Для macOS:
// - Откройте терминал и установите MongoDB с помощью Homebrew:
//   $ brew tap mongodb/brew
//   $ brew install mongodb-community
// - После завершения установки, вы можете запустить MongoDB командой:
//   $ brew services start mongodb-community

// Для Linux:
// - Установите MongoDB с помощью команды apt-get для Ubuntu:
//   $ sudo apt-get update
//   $ sudo apt-get install -y mongodb
// - Запустите MongoDB сервис:
//   $ sudo systemctl start mongodb

// 3. Настройте MongoDB
// MongoDB по умолчанию будет запускаться на порту 27017 и будет слушать соединения на локальном хосте.
// Вы можете изменить настройки, редактируя файл конфигурации MongoDB (обычно находится по пути /etc/mongod.conf для Linux и macOS, и C:\Program Files\MongoDB\Server\bin\mongod.cfg для Windows).

// 4. Проверьте установку
// Убедитесь, что MongoDB работает, запустив команду mongo в командной строке или терминале:
// $ mongo
// Вы должны увидеть приглашение MongoDB, что означает, что сервер запущен и готов к работе.

// 5. Установка MongoDB Compass (опционально)
// MongoDB Compass - это графический интерфейс для работы с MongoDB.
// Вы можете скачать MongoDB Compass с официального сайта MongoDB: https://www.mongodb.com/products/compass
// Установите его, следуя инструкциям на экране.

// Итог:
// Установка MongoDB включает в себя загрузку установочного файла, установку программного обеспечения, настройку и проверку работы сервера.
// MongoDB Compass предоставляет удобный интерфейс для визуального взаимодействия с вашей базой данных.
