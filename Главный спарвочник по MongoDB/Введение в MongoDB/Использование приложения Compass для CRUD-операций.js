// Глава 1: Введение в MongoDB
// Использование приложения Compass для CRUD-операций

// MongoDB - это нереляционная база данных, которая использует документы, похожие на JSON, для хранения данных.
// Приложение MongoDB Compass предоставляет графический интерфейс для работы с базой данных MongoDB.
// В этой главе мы рассмотрим, как использовать MongoDB Compass для выполнения операций CRUD (Создание, Чтение, Обновление и Удаление) с коллекциями и документами.

// 1. Подключение к базе данных
// Когда вы открываете Compass, первое, что нужно сделать - это подключиться к серверу MongoDB.
// Введите строку подключения (например, `mongodb://localhost:27017`) и нажмите "Connect".

// 2. Создание (Create)
// Чтобы добавить новый документ в коллекцию, откройте необходимую коллекцию и нажмите кнопку "Insert Document".
// В появившемся редакторе JSON введите новый документ и нажмите "Insert". MongoDB Compass сохранит документ в базе данных.
// Пример документа:
{
    "name": "John Doe",
    "age": 30,
    "email": "john.doe@example.com"
  }
  
  // MongoDB автоматически добавит поле `_id`, которое является уникальным идентификатором каждого документа.
  
  // 3. Чтение (Read)
  // Compass позволяет легко находить документы с помощью графического интерфейса. Выберите коллекцию и увидите все документы, хранящиеся в ней.
  // Чтобы фильтровать данные, можно использовать панель поиска в верхней части экрана.
  // Пример фильтрации документов по полю:
  {
    "age": { "$gte": 25 }
  }
  // Этот запрос вернет все документы, где поле `age` больше или равно 25.
  
  // 4. Обновление (Update)
  // MongoDB Compass позволяет обновлять существующие документы.
  // Чтобы обновить документ, выберите его, нажмите на значок "Edit" и внесите изменения в JSON. Затем нажмите "Update" для сохранения изменений.
  // Пример обновления документа:
  {
    "name": "John Doe",
    "age": 31 // обновляем возраст
  }
  
  // Также можно обновить несколько документов одновременно с помощью массового обновления (bulk update) через панель поиска.
  
  // 5. Удаление (Delete)
  // Чтобы удалить документ, выберите его и нажмите кнопку "Delete Document". Compass предложит подтверждение перед удалением.
  // Если нужно удалить несколько документов сразу, можно воспользоваться поиском и нажать "Delete" для всех найденных документов.
  
  // Итог:
  // MongoDB Compass - это мощный инструмент для выполнения CRUD-операций с базой данных MongoDB.
  // Он упрощает работу с документами и коллекциями, предоставляя интуитивно понятный интерфейс для создания, чтения, обновления и удаления данных.
  