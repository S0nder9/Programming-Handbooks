// Подглава 1.3: Операторы else и else if

// Синтаксис операторов else и else if
// Оператор else используется в паре с оператором if и выполняет блок кода, если условие в операторе if ложно.
// Оператор else if используется для проверки дополнительных условий, если предыдущие условия были ложны.

// Синтаксис оператора else:
// if (условие) {
//      выполнить этот блок кода, если условие истинно
// } else {
//      выполнить этот блок кода, если условие ложно
// }

// Синтаксис оператора else if:
// if (условие1) {
//      выполнить этот блок кода, если условие1 истинно
// } else if (условие2) {
//      выполнить этот блок кода, если условие2 истинно
// } else {
//      выполнить этот блок кода, если ни одно из условий не истинно
// }

// Примеры использования
const score = 85;

if (score >= 90) {
    console.log("Оценка: A");
} else if (score >= 80) {
    console.log("Оценка: B");
} else if (score >= 70) {
    console.log("Оценка: C");
} else if (score >= 60) {
    console.log("Оценка: D");
} else {
    console.log("Оценка: F");
}

// Пример с вложенными условиями
const num = 15;
if (num > 10) {
    if (num % 2 === 0) {
        console.log("Число больше 10 и четное");
    } else {
        console.log("Число больше 10 и нечетное");
    }
} else {
    console.log("Число меньше или равно 10");
}
