// Подглава 2.3: Сокращенные условия

// Использование логических операторов для коротких условий
// Короткий способ проверки условия с помощью логического И (&&).
let x = 5;
if (x > 0 && x < 10) {
    console.log("x находится в диапазоне от 0 до 10");
}

// Краткая запись условий с помощью логического И и тернарного оператора (?).
let y = 12;
let result = (y > 10) ? "Больше 10" : "Меньше или равно 10";
console.log(result); // Выведет "Больше 10"

// Примеры применения сокращенных условий
// Это полезно для упрощения кода и сокращения лишних строк.

// Пример сокращенного условия для вывода результата
let z = 15;
console.log((z > 10) ? "Больше 10" : "Меньше или равно 10"); // Выведет "Больше 10"

// Пример сокращенного условия для присвоения значения переменной
let isActive = true;
let status = (isActive) ? "Активен" : "Неактивен";
console.log(status); // Выведет "Активен"
