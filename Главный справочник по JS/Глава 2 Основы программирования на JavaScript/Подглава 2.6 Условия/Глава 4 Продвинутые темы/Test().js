// Подглава 4.4: Метод test()

// Метод test() - это метод объекта регулярного выражения, который используется для тестирования строки на соответствие регулярному выражению. 
// Он возвращает true, если строка соответствует регулярному выражению, и false в противном случае.

// Синтаксис:
// regex.test(string)

// Пример:

// Создание регулярного выражения для поиска слова "hello"
const regex = /hello/;

// Тестирование строки на соответствие регулярному выражению с помощью метода test()
const str1 = "hello world";
const str2 = "hi there";

// Тестирование первой строки
console.log(regex.test(str1)); // Вывод: true, так как строка содержит слово "hello"

// Тестирование второй строки
console.log(regex.test(str2)); // Вывод: false, так как строка не содержит слово "hello"

// Метод test() часто используется в условных операторах для проверки соответствия строки регулярному выражению:

if (regex.test(str1)) {
    console.log("Строка содержит слово 'hello'");
} else {
    console.log("Строка не содержит слово 'hello'");
}
