// Что такое объект в JavaScript?
/*
Объект в JavaScript - это составной тип данных, который представляет собой коллекцию ключ-значение. 
Ключи (свойства) являются строками, а значения могут быть любого типа данных, включая другие объекты, функции и примитивные значения.
Объекты в JavaScript могут быть созданы с помощью литералов объекта, оператора new или с использованием функций-конструкторов.
*/

// Свойства и методы объекта
/*
Свойства объекта - это пары ключ-значение, где ключ - это строка (или Symbol), а значение может быть любого типа данных.
Методы объекта - это функции, которые связаны с объектом и могут быть вызваны через точечную нотацию.
*/

// Пример объекта с свойствами и методами
const person = {
    name: "John",
    age: 30,
    greet: function() {
        console.log("Привет, меня зовут " + this.name);
    }
};

console.log(person.name); // Выведет: John
console.log(person.age); // Выведет: 30
person.greet(); // Выведет: Привет, меня зовут John

// Как создать объекты в JavaScript
/*
В JavaScript объекты могут быть созданы с помощью литералов объекта, оператора new или с использованием функций-конструкторов.
*/

// Создание объекта с помощью литерала объекта
const car = {
    brand: "Toyota",
    model: "Corolla"
};

// Создание объекта с использованием оператора new и функции-конструктора
function Car(brand, model) {
    this.brand = brand;
    this.model = model;
}

const myCar = new Car("Toyota", "Corolla");

// Создание объекта с помощью классов (ES6+)
class Animal {
    constructor(name) {
        this.name = name;
    }
}

const dog = new Animal("Rex");
