// Подглава 7.1: Объекты и колбэки

// Передача объектов в качестве колбэков
/*
Объекты могут быть переданы в качестве аргументов колбэк функций для обработки данных в асинхронном контексте.
*/

// Пример передачи объектов в качестве колбэков:
function fetchData(url, callback) {
    // Предположим, что здесь происходит асинхронный запрос данных по указанному URL
    // Вместо этого примера используем задержку для имитации асинхронной операции
    setTimeout(function() {
        const data = { id: 1, name: "John" };
        callback(data);
    }, 1000);
}

function processData(data) {
    console.log("Обработка данных:", data);
}

fetchData("https://example.com/data", processData);

// Изменение свойств объекта в асинхронном контексте
/*
Свойства объекта могут быть изменены внутри асинхронных операций, таких как обратные вызовы или промисы.
*/

// Пример изменения свойств объекта в асинхронном контексте:
const person = {
    name: "Alice",
    age: 25
};

function updatePerson() {
    // Предположим, что здесь происходит асинхронное обновление данных о персоне
    // Вместо этого примера используем задержку для имитации асинхронной операции
    setTimeout(function() {
        person.age = 26; // Изменение свойства объекта
        console.log("Данные о персоне обновлены:", person);
    }, 2000);
}

updatePerson();

