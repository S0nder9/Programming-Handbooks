// Подглава 3.1: Что такое делегирование событий

// Определение делегирования событий
// Делегирование событий - это метод обработки событий, при котором обработчик назначается родительскому элементу, вместо назначения обработчика каждому дочернему элементу отдельно. При этом события, происходящие внутри дочерних элементов, всплывают (bubbling) и могут быть обработаны родительским элементом или другими элементами вверх по иерархии DOM.

// Преимущества использования делегирования событий:
// 1. Эффективность: Позволяет уменьшить количество назначенных обработчиков событий, особенно на больших страницах с множеством элементов.
// 2. Динамичность: При добавлении или удалении дочерних элементов не требуется переназначать обработчики событий, так как события будут обрабатываться родительским элементом.
// 3. Удобство поддержки кода: Сокращает объем кода и облегчает его поддержку, так как логика обработки событий сосредоточена на родительском элементе.

// Пример:

// HTML-разметка

<div id="parent">
    <ul id="list">
        <li>Элемент 1</li>
        <li>Элемент 2</li>
        <li>Элемент 3</li>
    </ul>
</div>


// JavaScript-обработчик события с использованием делегирования
const parent = document.getElementById('parent');

parent.addEventListener('click', (event) => {
    // Проверяем, произошло ли событие на элементе списка
    if (event.target.tagName === 'LI') {
        // Обрабатываем событие, например, добавляем класс к элементу
        event.target.classList.toggle('selected');
    }
});
