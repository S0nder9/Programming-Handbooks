// Подглава 5.1: Определение пользовательских событий

// Создание и инициирование пользовательских событий
// Для создания пользовательского события используется конструктор CustomEvent.
// Этот конструктор принимает два аргумента: имя события и объект с опциями.
const customEvent = new CustomEvent('myCustomEvent', {
    bubbles: true, // Событие всплывает
    detail: {      // Пользовательские данные
        message: 'Hello from custom event!'
    }
});

// Инициирование пользовательского события
// Чтобы запустить пользовательское событие на определенном элементе,
// используйте метод dispatchEvent() и передайте в него созданное событие.
const element = document.getElementById('myElement');
element.dispatchEvent(customEvent);

// Регистрация обработчиков для пользовательских событий
// Для регистрации обработчиков пользовательских событий используйте метод addEventListener().
// Первым аргументом передается имя пользовательского события, а вторым - функция-обработчик.
element.addEventListener('myCustomEvent', function(event) {
    console.log('Пользовательское событие получено:', event.detail.message);
});
