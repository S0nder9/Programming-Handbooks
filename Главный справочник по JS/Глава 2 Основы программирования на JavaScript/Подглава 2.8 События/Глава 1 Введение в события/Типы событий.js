// Подглава 1.2: Типы событий

// Веб-разработка предоставляет множество типов событий, которые позволяют взаимодействовать с пользователем и реагировать на его действия. Основные типы событий включают в себя клики, наведения, изменения и другие.

// Категории событий могут быть разделены на несколько групп в зависимости от их источника или характера:

// 1. Пользовательские события: Эти события инициируются действиями пользователя, такими как клики мышью, нажатия клавиш, ввод текста и т. д.

// Примеры:
// - click: возникает при клике на элементе
// - keydown, keyup: возникает при нажатии и отпускании клавиши клавиатуры
// - input: возникает при вводе текста в элемент формы
// - submit: возникает при отправке формы

// 2. События клавиатуры: Эти события связаны с клавиатурными действиями пользователя и позволяют реагировать на нажатия клавиш.

// Примеры:
// - keydown, keyup: как упомянуто выше, они возникают при нажатии и отпускании клавиши
// - keypress: возникает при нажатии клавиши, но не регистрирует системные клавиши (например, Shift или Ctrl)

// 3. События мыши: Эти события возникают в ответ на действия пользователя с помощью мыши, такие как клики, движения и наведения.

// Примеры:
// - click: как упомянуто выше, возникает при клике на элементе
// - mouseover, mouseout: возникает при наведении курсора мыши на элемент и уходе курсора с элемента соответственно
// - mousedown, mouseup: возникает при нажатии и отпускании кнопки мыши

// И множество других типов событий, включая события фокусировки, изменения, загрузки и т. д.

// Понимание различных типов событий позволяет разработчикам создавать интерактивные веб-приложения и обеспечивать отзывчивость пользовательского интерфейса.

// Основные типы событий включают в себя широкий спектр действий, которые может совершать пользователь на веб-странице. Некоторые из них:
// - Клики (click): событие происходит при нажатии на элемент левой кнопкой мыши.
// - Наведения (mouseover, mouseout): события происходят, когда курсор мыши наводится на элемент и покидает его соответственно.
// - Изменения (change): событие происходит при изменении значения элемента формы (например, input, select).
// - Фокус (focus, blur): события происходят при получении или потере элементом фокуса ввода.
// - Загрузка (load): событие происходит, когда вся страница или определенный ресурс загружены.
// - Клавиатура (keydown, keyup): события происходят при нажатии и отпускании клавиши на клавиатуре соответственно.

// Категории событий:
// - Пользовательские события: связанные с действиями пользователя, такими как клики и наведения.
// - События клавиатуры: связанные с нажатиями и отпусканиями клавиш на клавиатуре.
// - События мыши: связанные с движениями и действиями мыши, такими как клики и наведения.
// - События offline/online: связанные с изменением состояния подключения к сети.
// - События контекстного меню (ПКМ): связанные с открытием контекстного меню правой кнопкой мыши.
// - События двойного клика (ДКЛИК): связанные с двойным щелчком левой кнопкой мыши.
// - События колеса мыши (КОЛЕСО МЫШИ): связанные с прокруткой колеса мыши.
// - Другие пользовательские типы событий могут быть созданы в соответствии с потребностями приложения.

// Каждый тип события предоставляет возможность реагировать на различные действия пользователя и управлять поведением веб-приложения в соответствии с этими действиями.
