// Подглава 5.1: Определение пользовательских событий

// Создание и инициирование пользовательских событий
// Пользовательские события - это события, которые создаются и инициируются разработчиком, а не браузером. Они позволяют расширить стандартный набор событий DOM и добавить кастомную функциональность к элементам.

// Чтобы создать пользовательское событие, используется конструктор CustomEvent. Затем событие инициируется на целевом элементе с помощью метода dispatchEvent.

// Пример создания и инициирования пользовательского события:
const customEvent = new CustomEvent('myEvent', {
    bubbles: true, // событие всплывает
    cancelable: true, // можно отменить действие события
    detail: { key: 'value' } // дополнительные данные
});

const targetElement = document.getElementById('myElement');
targetElement.dispatchEvent(customEvent);

// Регистрация обработчиков для пользовательских событий
// После создания и инициирования пользовательского события можно зарегистрировать обработчики событий для его обработки. Это делается с помощью метода addEventListener на целевом элементе.

// Пример регистрации обработчика пользовательского события:
const myElement = document.getElementById('myElement');

myElement.addEventListener('myEvent', function(event) {
    console.log('Сработал обработчик пользовательского события', event.detail);
});
