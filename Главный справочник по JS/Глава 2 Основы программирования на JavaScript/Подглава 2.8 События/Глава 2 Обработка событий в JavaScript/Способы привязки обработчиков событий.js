// Подглава 2.1: Способы привязки обработчиков событий

// События в JavaScript могут быть обработаны различными способами, включая как HTML-атрибуты, так и программное привязывание с использованием JavaScript.

// Атрибуты HTML-элементов для привязки обработчиков событий:
// HTML-элементы могут содержать атрибуты, такие как onclick, onmouseover, onkeydown и т. д., которые могут быть использованы для привязки JavaScript-функций к определенным событиям. Например:
 <button onclick="myFunction()">Нажми меня</button>

// Программное привязывание обработчиков событий с использованием JavaScript:
// Вместо использования атрибутов HTML-элементов, обработчики событий могут быть привязаны к элементам программно с помощью JavaScript. Это обеспечивает более чистый и управляемый код. Пример:
 const button = document.querySelector('button');
 button.addEventListener('click', myFunction);

// В обоих случаях функция myFunction будет вызвана при возникновении события click на элементе <button>. Однако второй подход предпочтителен с точки зрения отделения логики JavaScript от разметки HTML и повышения управляемости кода.