// Подглава 3.3: Изменение массива

// Метод join()

// Метод join() объединяет все элементы массива в одну строку, разделенную указанным разделителем, и возвращает полученную строку.

// Пример использования метода join():
const arr = ['Я', 'очень', 'люблю', 'программирование'];

// Объединяем элементы массива в строку, разделенную пробелами
const sentence = arr.join(' ');

console.log(sentence); // Выведет: "Я очень люблю программирование"
