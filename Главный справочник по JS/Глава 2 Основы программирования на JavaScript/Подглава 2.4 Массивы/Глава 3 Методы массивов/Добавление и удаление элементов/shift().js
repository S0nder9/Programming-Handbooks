// Метод shift()

// Метод shift() удаляет первый элемент из массива и возвращает его значение.

// Пример использования метода shift():
const arr = [1, 2, 3, 4];
const shiftedElement = arr.shift(); // удаляем первый элемент из массива
console.log(shiftedElement); // Выведет: 1
console.log(arr); // Выведет: [2, 3, 4]
