// Подглава 3.2: Добавление и удаление элементов

// Метод unshift()

// Метод unshift() добавляет один или несколько элементов в начало массива и возвращает новую длину массива.

// Пример использования метода unshift():
const arr = [2, 3, 4];
arr.unshift(1); // добавляем элемент 1 в начало массива
console.log(arr); // Выведет: [1, 2, 3, 4]
