// Подглава 3.5: Сортировка массива

// Метод reverse()

// Метод reverse() меняет порядок элементов массива на обратный и возвращает измененный массив.

// Пример использования метода reverse():
const arr = [1, 2, 3, 4, 5];
arr.reverse();
console.log(arr); // Выведет: [5, 4, 3, 2, 1] (элементы массива в обратном порядке)
