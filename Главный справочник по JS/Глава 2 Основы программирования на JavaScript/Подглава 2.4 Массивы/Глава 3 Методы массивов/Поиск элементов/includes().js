// Подглава 3.4: Поиск элементов

// Метод includes()

// Метод includes() проверяет, содержит ли массив указанный элемент, и возвращает true или false соответственно.

// Пример использования метода includes():
const arr = [1, 2, 3, 4, 5];

console.log(arr.includes(3)); // Выведет: true (массив содержит элемент 3)
console.log(arr.includes(6)); // Выведет: false (массив не содержит элемент 6)
