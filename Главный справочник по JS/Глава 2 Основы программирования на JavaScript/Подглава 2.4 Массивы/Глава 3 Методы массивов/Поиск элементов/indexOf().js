// Подглава 3.4: Поиск элементов

// Метод indexOf()

// Метод indexOf() возвращает индекс первого вхождения указанного элемента в массиве или -1, если элемент не найден.

// Пример использования метода indexOf():
const arr = [1, 2, 3, 4, 5];

console.log(arr.indexOf(3)); // Выведет: 2 (индекс элемента 3 в массиве)
console.log(arr.indexOf(6)); // Выведет: -1 (элемент 6 не найден в массиве)
