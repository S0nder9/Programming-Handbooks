// Подглава 3.4: Поиск элементов

// Метод lastIndexOf()

// Метод lastIndexOf() возвращает индекс последнего вхождения указанного элемента в массиве или -1, если элемент не найден.

// Пример использования метода lastIndexOf():
const arr = [1, 2, 3, 4, 5, 3];

console.log(arr.lastIndexOf(3)); // Выведет: 5 (индекс последнего вхождения элемента 3 в массиве)
console.log(arr.lastIndexOf(6)); // Выведет: -1 (элемент 6 не найден в массиве)
