// Подглава 1.2: Цикл while

// Синтаксис цикла while
// Цикл while выполняет блок кода, пока указанное условие истинно.
// Синтаксис:
// while (условие) {
//     блок кода
// }

// Примеры использования цикла while
// Пример 1: Вывод чисел от 1 до 5
let i = 1;
while (i <= 5) {
    console.log(i);
    i++;
}

// Пример 2: Поиск элемента в массиве
const numbers = [1, 2, 3, 4, 5];
let index = 0;
while (index < numbers.length) {
    if (numbers[index] === 3) {
        console.log("Элемент найден!");
        break; // Выход из цикла после нахождения элемента
    }
    index++;
}

// Управление выполнением цикла
// В цикле while можно использовать операторы break и continue для управления выполнением цикла.
// Оператор break завершает выполнение цикла, а оператор continue переходит к следующей итерации.
