// Подглава 8.2: Методы объектов и контекст выполнения

// Определение методов объектов
/*
Метод объекта - это функция, связанная с объектом и вызываемая через точечную нотацию или квадратные скобки.
Методы объектов используются для изменения состояния объекта или выполнения действий, связанных с ним.
*/

// Пример определения методов объектов
const car = {
    brand: "Toyota",
    model: "Camry",
    year: 2020,
    start: function() {
        console.log("Двигатель запущен");
    },
    stop: function() {
        console.log("Двигатель остановлен");
    }
};

car.start(); // Выведет: Двигатель запущен
car.stop(); // Выведет: Двигатель остановлен

/*
В этом примере объект car содержит два метода: start и stop, которые выполняют соответствующие действия.
*/

// Привязка контекста выполнения к методам объектов
/*
Контекст выполнения (this) в методах объектов ссылается на сам объект, через который метод был вызван.
Привязка контекста выполнения может быть изменена с помощью методов call(), apply() и bind().
*/

// Пример привязки контекста выполнения к методам объектов
const person = {
    name: "John",
    age: 30,
    greet: function() {
        console.log("Привет, меня зовут " + this.name);
    }
};

const anotherPerson = {
    name: "Alice"
};

person.greet(); // Выведет: Привет, меня зовут John
person.greet.call(anotherPerson); // Выведет: Привет, меня зовут Alice

/*
В этом примере метод greet объекта person использует контекст выполнения this, чтобы получить доступ к свойству name этого объекта.
С помощью метода call() контекст выполнения this в методе greet может быть изменен на объект anotherPerson, что позволяет использовать его свойство name.
*/
