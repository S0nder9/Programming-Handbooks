// Подглава 8.1: Функции как объекты первого класса

// Возможности функций в качестве объектов

/*
Функцикции в JavaScript являются объектами первого класса, что означает, что они могут быть:
1. Присвоены переменным и свойствам объектов.
2. Переданы как аргументы в другие функции.
3. Возвращены из других функций.
4. Созданы динамически во время выполнения программы.
*/

// Пример присвоения функции переменной
const greet = function() {
    console.log("Привет, мир!");
};

// Пример передачи функции как аргумента
function callFunction(func) {
    func();
}

callFunction(greet); // Выведет: Привет, мир!

// Пример возврата функции из другой функции
function createCounter() {
    let count = 0;
    return function() {
        count++;
        console.log(count);
    };
}

const counter = createCounter();
counter(); // Выведет: 1
counter(); // Выведет: 2

/*
В этом примере функция createCounter возвращает другую функцию, которая увеличивает счетчик при каждом вызове.
Это демонстрирует возможность возврата функций из других функций.
*/
