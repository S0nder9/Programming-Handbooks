// Переменные в JavaScript

// Переменные в JavaScript используются для хранения данных. 
// В JavaScript переменные объявляются с помощью ключевого слова 'let', 'var' или 'const'.

// 1. Объявление переменной с помощью 'let':
let x = 10; // Объявление переменной 'x' и присваивание ей значения 10
let message = 'Привет, мир!'; // Объявление переменной 'message' и присваивание ей строки

// 2. Объявление переменной с помощью 'var' (устаревший способ):
var y = 5; // Объявление переменной 'y' и присваивание ей значения 5

// 3. Объявление константы с помощью 'const':
const PI = 3.14; // Объявление константы 'PI' и присваивание ей значения 3.14

// Переменные могут содержать различные типы данных, такие как числа, строки, булевы значения, массивы, объекты и т. д.

// Примеры использования переменных:
let firstName = 'John';
let lastName = 'Doe';
let fullName = firstName + ' ' + lastName; // Конкатенация строк

let age = 30;
let isAdult = age >= 18; // Проверка на совершеннолетие

let numbers = [1, 2, 3, 4, 5]; // Массив чисел
let personMain = { name: 'Alice', age: 25 }; // Объект с данными о человеке

// Переменные могут изменять своё значение в процессе выполнения программы:
let counter = 0;
counter++; // Увеличение значения переменной 'counter' на 1

// Переменные в JavaScript имеют область видимости и могут быть глобальными или локальными в зависимости от места их объявления.
// Также переменные могут быть объявлены без явного указания значения, в таком случае они будут равны 'undefined'.

// Переменные могут использоваться в выражениях и взаимодействовать с другими частями программы для выполнения различных задач.

// В JavaScript рекомендуется использовать ключевые слова 'let' или 'const' для объявления переменных вместо 'var', 
// так как 'let' и 'const' имеют блочную область видимости, что способствует более предсказуемому поведению кода.

// Примеры объявления переменных:
let a = 10; // Целочисленная переменная
let b = 'Hello'; // Строковая переменная
const c = 3.14; // Константа

// Переменные могут быть объявлены в любом месте программы, но доступ к ним возможен только после их объявления.

// Переменные в JavaScript являются динамически типизированными, что означает, что их тип данных может изменяться в процессе выполнения программы.

// Пример использования переменных в арифметическом выражении:
let num1 = 5;
let num2 = 3;
let sum = num1 + num2; // Результат: 8

// Переменные могут быть использованы в условных выражениях для принятия решений в программе:
let temperature = 25;
if (temperature > 20) {
    console.log('Погода теплая');
} else {
    console.log('Погода прохладная');
}

// Переменные также могут быть использованы для итерации по массивам или объектам:
let fruits = ['apple', 'banana', 'orange'];
for (let i = 0; i < fruits.length; i++) {
    console.log(fruits[i]);
}

let person = { name: 'John', age: 30 };
for (let key in person) {
    console.log(key + ': ' + person[key]);
}

// В JavaScript переменные являются одним из основных инструментов для хранения и обработки данных, необходимых для работы программы.

// Важно помнить, что имена переменных в JavaScript чувствительны к регистру и могут содержать буквы, цифры, символы $ и _, 
// но не могут начинаться с цифры и не могут содержать пробелы.

// Также рекомендуется выбирать осмысленные имена переменных, которые ясно отражают их предназначение и содержимое.

