// Выражения присваивания

// Выражения присваивания в JavaScript используются для изменения значения переменной с помощью операторов присваивания.

// Примеры выражений присваивания:
let a = 5; // Присваивание значения 5 переменной 'a'
let b = 10; // Присваивание значения 10 переменной 'b'

a += 2; // Увеличение значения переменной 'a' на 2, теперь 'a' равно 7
b -= 3; // Уменьшение значения переменной 'b' на 3, теперь 'b' равно 7
