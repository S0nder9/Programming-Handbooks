// Глава 2: Основы работы с Canvas
// Подглава 2.2: Контекст рисования

// HTML5 Canvas предоставляет мощные возможности для рисования графики в веб-приложениях.
// Основной элемент для работы с Canvas - это сам <canvas> элемент в HTML, который используется для отрисовки графики с помощью JavaScript.

// Получение 2D-контекста для рисования:
// Чтобы начать рисовать на Canvas, необходимо получить контекст рисования.
// В большинстве случаев используется 2D-контекст, который позволяет рисовать 2D-графику.

// Пример получения 2D-контекста:
const canvas = document.getElementById('myCanvas');
const ctx = canvas.getContext('2d');

// Основные методы и свойства контекста 2D:
// Контекст 2D предоставляет множество методов и свойств для рисования на Canvas.

// 1. Рисование прямоугольников:
// - fillRect(x, y, width, height): Рисует заполненный прямоугольник.
// - strokeRect(x, y, width, height): Рисует прямоугольник с контуром.
// - clearRect(x, y, width, height): Очищает прямоугольник, делая его прозрачным.

ctx.fillStyle = 'blue'; // Задает цвет заливки
ctx.fillRect(10, 10, 100, 50); // Рисует синий заполненный прямоугольник

ctx.strokeStyle = 'red'; // Задает цвет контура
ctx.strokeRect(130, 10, 100, 50); // Рисует красный прямоугольник с контуром

ctx.clearRect(10, 10, 50, 25); // Очищает часть прямоугольника

// 2. Рисование линий:
// - beginPath(): Начинает новый путь.
// - moveTo(x, y): Перемещает перо к указанным координатам.
// - lineTo(x, y): Рисует линию до указанных координат.
// - stroke(): Рисует контур по текущему пути.

ctx.beginPath();
ctx.moveTo(10, 80); // Перемещает перо к точке (10, 80)
ctx.lineTo(110, 80); // Рисует линию до точки (110, 80)
ctx.lineTo(110, 130); // Рисует линию до точки (110, 130)
ctx.stroke(); // Рисует контур по пути

// 3. Рисование окружностей и дуг:
// - arc(x, y, radius, startAngle, endAngle, anticlockwise): Рисует дугу/окружность.

ctx.beginPath();
ctx.arc(200, 100, 50, 0, Math.PI * 2, false); // Рисует окружность
ctx.stroke(); // Рисует контур окружности

// 4. Работа с текстом:
// - font: Задает стиль шрифта для текста.
// - fillText(text, x, y): Рисует заполненный текст.
// - strokeText(text, x, y): Рисует текст с контуром.

ctx.font = '20px Arial';
ctx.fillText('Hello Canvas', 10, 160); // Рисует заполненный текст
ctx.strokeText('Hello Canvas', 10, 190); // Рисует текст с контуром

// Итог:
// Контекст 2D в Canvas предоставляет множество методов и свойств для рисования графики.
// Эти методы позволяют рисовать прямоугольники, линии, окружности и текст, а также задавать стили для заполнения и контуров.
