// Глава 1: Введение в Canvas

// Подглава 1.1: Что такое Canvas

// Определение и назначение Canvas:
// Canvas - это HTML-элемент, который используется для рисования графики с помощью JavaScript.
// Он предоставляет двумерный контекст для создания различных видов графики, включая фигуры, текст, изображения и анимации.
// Canvas элемент является частью HTML5 и широко используется для динамического и интерактивного отображения контента в веб-приложениях.

// Пример создания элемента Canvas в HTML:
<canvas id="myCanvas" width="500" height="500"></canvas>

// Чтобы нарисовать что-то на Canvas, необходимо получить его контекст в JavaScript:
const canvas = document.getElementById('myCanvas');
const context = canvas.getContext('2d');

// Пример рисования прямоугольника на Canvas:
context.fillStyle = 'blue';
context.fillRect(50, 50, 150, 100);

// Преимущества использования Canvas в веб-разработке:

// 1. Высокая производительность:
// Canvas позволяет рисовать графику напрямую с использованием аппаратного ускорения,
// что делает его идеальным для создания игр, анимаций и других интенсивных графических приложений.

// 2. Гибкость:
// Canvas предоставляет низкоуровневый API для рисования графики, что позволяет создавать
// любые виды визуальных эффектов и графики, ограничиваясь только вашей фантазией и навыками программирования.

// 3. Интерактивность:
// С помощью Canvas можно создавать интерактивные элементы, такие как диаграммы, графики и пользовательские интерфейсы,
// которые реагируют на действия пользователя, такие как клики мыши или касания.

// 4. Поддержка анимации:
// Canvas позволяет создавать плавные анимации с использованием JavaScript, что делает его
// идеальным выбором для разработки динамического и интерактивного контента на веб-страницах.

// Пример анимации на Canvas:
function drawCircle(x, y, radius) {
  context.clearRect(0, 0, canvas.width, canvas.height);
  context.beginPath();
  context.arc(x, y, radius, 0, Math.PI * 2, false);
  context.fillStyle = 'red';
  context.fill();
  context.closePath();
}

let x = 50;
let y = 50;
let radius = 30;

function animate() {
  drawCircle(x, y, radius);
  x += 1;
  y += 1;
  requestAnimationFrame(animate);
}

animate();

// Итог:
// HTML-элемент Canvas предоставляет мощный инструмент для создания графики и анимаций на веб-страницах.
// Его гибкость, высокая производительность и поддержка интерактивности делают его незаменимым для современных веб-разработчиков.
