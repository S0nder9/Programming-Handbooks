// Глава 9: Работа с пикселями в Canvas

// Подглава 9.1: Доступ к пиксельным данным

// Canvas API предоставляет методы для работы с пиксельными данными на холсте.
// Основные методы для получения и установки пиксельных данных - это getImageData() и putImageData().

// getImageData() - используется для получения данных изображения из заданной области холста.
// putImageData() - используется для установки данных изображения в заданную область холста.

// Пример использования getImageData():
const canvas = document.getElementById('myCanvas');
const ctx = canvas.getContext('2d');

// Рисуем красный прямоугольник на холсте
ctx.fillStyle = 'red';
ctx.fillRect(10, 10, 100, 100);

// Получаем данные изображения из прямоугольника
const imageData = ctx.getImageData(10, 10, 100, 100);

// Данные изображения хранятся в массиве Uint8ClampedArray
// Каждый пиксель представлен четырьмя значениями: красный, зеленый, синий и альфа (RGBA)
console.log(imageData.data);

// Пример использования putImageData():
// Создаем новые данные изображения
const newImageData = ctx.createImageData(100, 100);

// Заполняем новые данные изображения синим цветом
for (let i = 0; i < newImageData.data.length; i += 4) {
  newImageData.data[i] = 0;     // Красный
  newImageData.data[i + 1] = 0; // Зеленый
  newImageData.data[i + 2] = 255; // Синий
  newImageData.data[i + 3] = 255; // Альфа (непрозрачный)
}

// Устанавливаем новые данные изображения на холст
ctx.putImageData(newImageData, 120, 10);

// Итог:
// getImageData() и putImageData() позволяют получать и устанавливать пиксельные данные на холсте.
// Эти методы полезны для работы с низкоуровневыми операциями изображения, такими как фильтры или манипуляции с пикселями.
