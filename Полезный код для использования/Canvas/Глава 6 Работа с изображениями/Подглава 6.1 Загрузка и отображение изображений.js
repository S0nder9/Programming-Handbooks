// Глава 6: Работа с изображениями в Canvas

// Подглава 6.1: Загрузка и отображение изображений

// В HTML5 Canvas API `drawImage()` - это метод, который позволяет рисовать изображения на холсте.
// Этот метод можно использовать для загрузки изображений и их отображения на холсте. Он также поддерживает различные варианты использования, включая обрезку изображений и масштабирование.

// Как использовать drawImage():

// 1. Создайте элемент Canvas в HTML и получите контекст рисования:
const canvas = document.getElementById('myCanvas');
const ctx = canvas.getContext('2d');

// 2. Создайте объект Image и укажите источник изображения:
const image = new Image();
image.src = 'path/to/your/image.jpg'; // Укажите путь к вашему изображению

// 3. Загрузите изображение и вызовите drawImage() после того, как изображение загрузится:
image.onload = () => {
  // Используйте drawImage() для отображения изображения на холсте
  // drawImage(image, x, y, width, height) - Рисует изображение на холсте с указанными координатами и размерами
  ctx.drawImage(image, 0, 0, canvas.width, canvas.height); // Изображение растягивается по всему холсту
};

// drawImage() также поддерживает различные варианты использования:

// 1. Отображение изображения с учетом его размеров:
ctx.drawImage(image, 50, 50); // Рисует изображение на позиции (50, 50) без изменения размера

// 2. Масштабирование изображения при его отображении:
ctx.drawImage(image, 50, 50, 200, 150); // Рисует изображение на позиции (50, 50) с шириной 200 и высотой 150

// 3. Обрезка части изображения и ее отображение:
ctx.drawImage(image, 0, 0, 100, 100, 50, 50, 200, 200); // Рисует часть изображения с (0, 0) до (100, 100) в позиции (50, 50) с размерами 200x200

// 4. Использование drawImage() с объектом canvas:
const offScreenCanvas = document.createElement('canvas');
const offScreenCtx = offScreenCanvas.getContext('2d');
offScreenCanvas.width = 100;
offScreenCanvas.height = 100;
offScreenCtx.drawImage(image, 0, 0, 100, 100);
ctx.drawImage(offScreenCanvas, 150, 150); // Рисует изображение с offScreenCanvas на основном холсте

// Итог:
// Метод `drawImage()` является основным инструментом для работы с изображениями в Canvas.
// Он позволяет загружать изображения, изменять их размер, обрезать и отображать на холсте.
// Используйте различные варианты `drawImage()` для достижения нужных эффектов и отображения изображений.

