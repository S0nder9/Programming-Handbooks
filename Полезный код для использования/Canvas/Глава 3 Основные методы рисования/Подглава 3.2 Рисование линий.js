// Глава 3: Основные методы рисования
// Подглава 3.2: Рисование линий - beginPath(), moveTo(), lineTo(), stroke()

// В HTML5 Canvas API есть набор методов для рисования графики на веб-странице.
// В этой подглаве мы рассмотрим основные методы для рисования линий на холсте: `beginPath()`, `moveTo()`, `lineTo()`, и `stroke()`.

// 1. Метод `beginPath()`:
// Этот метод начинает новый путь или сбрасывает текущий путь на холсте.
// Он необходим для того, чтобы изолировать линии и формы, чтобы их можно было рисовать отдельно друг от друга.

const canvas = document.getElementById('myCanvas');
const ctx = canvas.getContext('2d');

ctx.beginPath(); // Начинаем новый путь

// 2. Метод `moveTo(x, y)`:
// Этот метод перемещает перо на указанные координаты (x, y) без рисования линии.
// Он устанавливает начальную точку для следующей линии или фигуры.

ctx.moveTo(50, 50); // Перемещаем перо в точку (50, 50)

// 3. Метод `lineTo(x, y)`:
// Этот метод рисует линию от текущей позиции пера до указанных координат (x, y).

ctx.lineTo(200, 50); // Рисуем линию от (50, 50) до (200, 50)
ctx.lineTo(200, 200); // Рисуем линию от (200, 50) до (200, 200)
ctx.lineTo(50, 200); // Рисуем линию от (200, 200) до (50, 200)
ctx.lineTo(50, 50); // Рисуем линию от (50, 200) до (50, 50)

// 4. Метод `stroke()`:
// Этот метод применяет настройки стиля к текущему пути и отрисовывает его на холсте.
// Он завершает путь и наносит линии на холст.

ctx.stroke(); // Рисуем линии, образующие замкнутый квадрат

// Пример полного кода для рисования квадрата:

const canvas = document.getElementById('myCanvas');
const ctx = canvas.getContext('2d');

ctx.beginPath(); // Начинаем новый путь
ctx.moveTo(50, 50); // Перемещаем перо в точку (50, 50)
ctx.lineTo(200, 50); // Рисуем линию до (200, 50)
ctx.lineTo(200, 200); // Рисуем линию до (200, 200)
ctx.lineTo(50, 200); // Рисуем линию до (50, 200)
ctx.lineTo(50, 50); // Рисуем линию до начальной точки (50, 50)
ctx.stroke(); // Отрисовываем линии на холсте

// Итог:
// Методы `beginPath()`, `moveTo()`, `lineTo()`, и `stroke()` являются основными инструментами для рисования линий на холсте.
// `beginPath()` начинает новый путь, `moveTo()` устанавливает начальную точку, `lineTo()` рисует линии, а `stroke()` отрисовывает путь на холсте.
// Эти методы часто используются вместе для создания различных графических элементов на веб-странице.
