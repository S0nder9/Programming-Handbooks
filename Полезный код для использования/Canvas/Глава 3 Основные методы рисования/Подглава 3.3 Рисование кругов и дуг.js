// Глава 3: Основные методы рисования

// Подглава 3.3: Рисование кругов и дуг - arc(), arcTo()

// В HTML5 Canvas API есть несколько методов для рисования кругов и дуг.
// Эти методы позволяют создавать круглые формы и различные сегменты окружностей.

// 1. Метод arc()

// Метод arc() используется для рисования части окружности или полного круга.
// Он принимает параметры, которые задают координаты центра, радиус, углы и направление рисования.

// Синтаксис:
// context.arc(x, y, radius, startAngle, endAngle, anticlockwise);

// Параметры:
// - x, y: Координаты центра окружности.
// - radius: Радиус окружности.
// - startAngle: Начальный угол (в радианах) от горизонтальной оси.
// - endAngle: Конечный угол (в радианах) от горизонтальной оси.
// - anticlockwise: Направление рисования (true - против часовой стрелки, false - по часовой стрелке).

// Пример использования метода arc():
const canvas = document.createElement('canvas');
const context = canvas.getContext('2d');

// Рисуем полный круг с центром в (100, 100), радиусом 50, от 0 до 2π радиан, по часовой стрелке
context.beginPath();
context.arc(100, 100, 50, 0, 2 * Math.PI, false);
context.strokeStyle = 'blue';
context.lineWidth = 5;
context.stroke();

// Рисуем дугу от 0 до π радиан (половина круга)
context.beginPath();
context.arc(100, 100, 50, 0, Math.PI, false);
context.strokeStyle = 'red';
context.lineWidth = 5;
context.stroke();

// 2. Метод arcTo()

// Метод arcTo() используется для рисования дуги между двумя линиями, образующими угол.
// Он принимает параметры, которые задают координаты двух конечных точек и радиус дуги.

// Синтаксис:
// context.arcTo(x1, y1, x2, y2, radius);

// Параметры:
// - x1, y1: Координаты первой точки.
// - x2, y2: Координаты второй точки.
// - radius: Радиус дуги.

// Пример использования метода arcTo():
context.beginPath();
context.moveTo(50, 50); // Перемещаемся в точку (50, 50)
context.lineTo(150, 50); // Рисуем линию до точки (150, 50)
context.arcTo(150, 50, 100, 100, 50); // Рисуем дугу с радиусом 50 от точки (150, 50) до точки (100, 100)
context.strokeStyle = 'green';
context.lineWidth = 5;
context.stroke();

// Итог:
// Методы arc() и arcTo() предоставляют основные возможности для рисования кругов и дуг на Canvas.
// Метод arc() полезен для создания окружностей и сегментов окружностей, а метод arcTo() - для создания плавных переходов между линиями.
// Знание этих методов позволяет создавать сложные графические элементы и визуализировать данные в Canvas.
