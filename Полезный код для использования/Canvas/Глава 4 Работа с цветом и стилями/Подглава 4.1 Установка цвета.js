// Глава 4: Работа с цветом и стилями
// Подглава 4.1: Установка цвета - fillStyle и strokeStyle

// В JavaScript работа с цветом и стилями часто выполняется с помощью Canvas API.
// Canvas API позволяет рисовать на элементе <canvas> в HTML, используя контекст рисования 2D или WebGL.
// В контексте 2D, для управления цветом используются свойства `fillStyle` и `strokeStyle`.

// 1. fillStyle - Установка цвета заливки

// Свойство `fillStyle` определяет цвет или стиль заливки для рисования фигур, таких как прямоугольники, круги и текст.
// Цвет можно задать как строку с именем цвета, кодом цвета в формате HEX, RGB, RGBA или HSL.

const canvas = document.getElementById('myCanvas');
const ctx = canvas.getContext('2d');

// Установка цвета заливки с использованием имени цвета
ctx.fillStyle = 'red';
ctx.fillRect(10, 10, 100, 100); // Рисует красный квадрат

// Установка цвета заливки с использованием HEX кода
ctx.fillStyle = '#00FF00';
ctx.fillRect(120, 10, 100, 100); // Рисует зеленый квадрат

// Установка цвета заливки с использованием RGB
ctx.fillStyle = 'rgb(0, 0, 255)';
ctx.fillRect(230, 10, 100, 100); // Рисует синий квадрат

// Установка цвета заливки с использованием RGBA (с прозрачностью)
ctx.fillStyle = 'rgba(255, 165, 0, 0.5)';
ctx.fillRect(340, 10, 100, 100); // Рисует оранжевый квадрат с прозрачностью 50%

// Установка цвета заливки с использованием HSL
ctx.fillStyle = 'hsl(120, 100%, 50%)';
ctx.fillRect(450, 10, 100, 100); // Рисует ярко-зеленый квадрат

// 2. strokeStyle - Установка цвета обводки

// Свойство `strokeStyle` определяет цвет или стиль обводки для рисования контуров фигур.
// Цвет и стиль можно задать так же, как и для `fillStyle`.

ctx.strokeStyle = 'blue';
ctx.lineWidth = 5; // Установка ширины линии для обводки
ctx.strokeRect(10, 120, 100, 100); // Рисует синий контур вокруг квадрата

// Установка цвета обводки с использованием HEX кода
ctx.strokeStyle = '#FF0000';
ctx.strokeRect(120, 120, 100, 100); // Рисует красный контур вокруг квадрата

// Установка цвета обводки с использованием RGB
ctx.strokeStyle = 'rgb(255, 0, 255)';
ctx.strokeRect(230, 120, 100, 100); // Рисует фиолетовый контур вокруг квадрата

// Установка цвета обводки с использованием RGBA (с прозрачностью)
ctx.strokeStyle = 'rgba(0, 255, 255, 0.7)';
ctx.strokeRect(340, 120, 100, 100); // Рисует бирюзовый контур с прозрачностью 70%

// Установка цвета обводки с использованием HSL
ctx.strokeStyle = 'hsl(0, 100%, 50%)';
ctx.strokeRect(450, 120, 100, 100); // Рисует красный контур вокруг квадрата

// 3. Пример комбинации fillStyle и strokeStyle

// Установка комбинации fillStyle и strokeStyle для кругов
ctx.beginPath();
ctx.arc(150, 250, 50, 0, Math.PI * 2, false); // Рисует круг
ctx.fillStyle = 'green';  // Устанавливаем цвет заливки
ctx.fill();  // Заливаем круг зеленым цветом
ctx.strokeStyle = 'black';  // Устанавливаем цвет обводки
ctx.lineWidth = 3;  // Устанавливаем ширину линии
ctx.stroke();  // Обводим круг черной линией

// Итог:
// Свойства `fillStyle` и `strokeStyle` используются для управления цветом и стилем рисуемых фигур на Canvas.
// `fillStyle` управляет цветом заливки, а `strokeStyle` - цветом и стилем обводки.
// Эти свойства можно настроить с помощью различных форматов цвета, включая имена, HEX, RGB, RGBA и HSL.
