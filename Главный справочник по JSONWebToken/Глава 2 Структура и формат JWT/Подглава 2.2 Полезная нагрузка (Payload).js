// Подглава 2.2: Полезная нагрузка (Payload)

// В JWT (JSON Web Token) полезная нагрузка (Payload) содержит информацию, которую вы хотите передать между сторонами.
// Это основная часть токена, где хранятся все данные, и она может включать как стандартные, так и нестандартные поля.

// Содержимое полезной нагрузки: стандартные и нестандартные поля

// Полезная нагрузка представляет собой объект JSON, который кодируется и включается в токен.
// Существует несколько стандартных полей, которые можно использовать в полезной нагрузке, а также возможность добавления нестандартных полей для передачи дополнительных данных.

// Примеры стандартных полей:

// 1. iss (issuer) - определяет издателя токена.
const payload = {
    iss: 'https://yourdomain.com'
  };
  
  // 2. sub (subject) - указывает на субъекта токена, например, идентификатор пользователя.
  const payload = {
    sub: '1234567890'
  };
  
  // 3. aud (audience) - определяет аудиторию, для которой предназначен токен.
  const payload = {
    aud: 'your-audience-identifier'
  };
  
  // 4. exp (expiration) - указывает время истечения срока действия токена в виде UNIX timestamp.
  const payload = {
    exp: Math.floor(Date.now() / 1000) + (60 * 60) // Токен истечет через час
  };
  
  // Использование нестандартных полей для передачи данных
  
  // Помимо стандартных полей, вы можете добавлять любые другие поля в полезную нагрузку для передачи специфической информации.
  // Это могут быть данные пользователя, роли, права доступа и многое другое.
  
  // Пример нестандартных полей:
  const payload = {
    iss: 'https://yourdomain.com',
    sub: '1234567890',
    name: 'John Doe',
    role: 'admin',
    permissions: ['read', 'write', 'delete']
  };
  
  // Эти нестандартные поля позволяют вам гибко адаптировать JWT под нужды вашего приложения,
  // передавая необходимую информацию, которая будет использоваться на стороне сервера или клиента.
  
  // Итог:
  // Полезная нагрузка (Payload) в JWT содержит как стандартные, так и нестандартные поля.
  // Стандартные поля обеспечивают базовую информацию о токене, а нестандартные поля позволяют передавать дополнительные данные,
  // делая JWT универсальным инструментом для аутентификации и авторизации.
  